def extract_speaker_embedding(audio_path, target_dim=512):
    if not os.path.exists(audio_path):
        raise FileNotFoundError(f"Audio file not found: {audio_path}")
    signal, fs = torchaudio.load(audio_path)
    if fs != 16000:
        print(f"   Resampling from {fs} Hz to 16000 Hz")
        resampler = torchaudio.transforms.Resample(fs, 16000)
        signal = resampler(signal)
    # signal shape: [channels, samples] -> make it mono if needed
    if signal.shape[0] > 1:
        signal = torch.mean(signal, dim=0, keepdim=True)
    # ECAPA encode_batch expects shape [batch, time] -> [1, time]
    embedding = classifier_ecapa.encode_batch(signal)  # [1, 1, 192]
    emb = embedding.squeeze()  # squeeze all dims -> [192] or scalar if empty
    if emb.dim() == 0:
        raise ValueError("Embedding extraction failed; got scalar.")
    # Pad to target_dim
    if emb.shape[0] < target_dim:
        emb = torch.nn.functional.pad(emb, (0, target_dim - emb.shape[0]))
    else:
        emb = emb[:target_dim]
    # Ensure shape is [1, target_dim] for SpeechT5
    return emb.unsqueeze(0)  # [1, 512]

