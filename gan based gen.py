#  Helper: Compute MCD (Mel Cepstral Distortion)
def compute_mcd(audio1, audio2, sr=16000, n_mfcc=13):
    """
    Compute MCD between two audio signals.
    Audio1: reference (your original voice)
    Audio2: spoofed sample
    """
    try:
        mfcc1 = librosa.feature.mfcc(y=audio1, sr=sr, n_mfcc=n_mfcc)
        mfcc2 = librosa.feature.mfcc(y=audio2, sr=sr, n_mfcc=n_mfcc)
        # Align to the shorter sequence
        min_len = min(mfcc1.shape[1], mfcc2.shape[1])
        mfcc1 = mfcc1[:, :min_len]
        mfcc2 = mfcc2[:, :min_len]
        diff = np.sqrt(np.sum((mfcc1 - mfcc2) ** 2, axis=0))
        return float(np.mean(diff))
    except Exception as e:
        print(f"    MCD computation failed: {e}")
        return 99.0

