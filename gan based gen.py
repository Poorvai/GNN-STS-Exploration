#  5. Emotion simulation function -
def generate_emotional_speech(text, speaker_emb, emotion="neutral", output_path=None):
    """
    Generate speech with the given speaker embedding,
    then apply pitch & speed changes to simulate emotion (if pyrubberband available).
    """
    inputs = processor(text=text, return_tensors="pt")
    with torch.no_grad():
        speech = model.generate_speech(
            inputs["input_ids"],
            speaker_emb,  # already shape [1, 512]
            vocoder=vocoder
        )
    speech_np = speech.squeeze().cpu().numpy()
    sr = 16000

    # Emotion presets
    presets = {
        "neutral":  (0.0, 1.0),
        "happy":    (+2.0, 1.15),
        "sad":      (-2.5, 0.85),
        "angry":    (+1.5, 1.05),
        "excited":  (+4.0, 1.25),
        "calm":     (-1.0, 0.95)
    }
    pitch_shift, speed_factor = presets.get(emotion, (0.0, 1.0))

    if emotion != "neutral" and RUBBERBAND_AVAILABLE:
        try:
            speech_mod = pyrb.pitch_shift(speech_np, sr, n_steps=pitch_shift)
            speech_mod = pyrb.time_stretch(speech_mod, sr, speed_factor)
        except Exception as e:
            print(f"    pyrubberband failed: {e}. Using neutral output.")
            speech_mod = speech_np
    else:
        speech_mod = speech_np

    if output_path:
        sf.write(output_path, speech_mod, sr)
    return speech_mod, sr

