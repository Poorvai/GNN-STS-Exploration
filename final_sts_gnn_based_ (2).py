class AudioProcessor:
    def __init__(self, cfg: AudioConfig):
        self.cfg = cfg
        self.sr = cfg.sample_rate

    def load_audio(self, path: str) -> torch.Tensor:
        try:
            sr_file, data = wav_io.read(path)
            if data.dtype == np.int16:   data = data.astype(np.float32) / 32768.0
            elif data.dtype == np.int32: data = data.astype(np.float32) / 2147483648.0
            else: data = data.astype(np.float32)
            if data.ndim == 2: data = data.mean(axis=1)
            wav = torch.from_numpy(data).unsqueeze(0)
            if sr_file != self.sr:
                wav = T.Resample(sr_file, self.sr)(wav)
        except Exception:
            wav, sr_file = torchaudio.load(path)
            if sr_file != self.sr: wav = T.Resample(sr_file, self.sr)(wav)
        if wav.shape[0] > 1: wav = wav.mean(0, keepdim=True)
        wav = wav / (wav.abs().max() + 1e-8)
        return wav

    def wav_to_mel_linear(self, wav: torch.Tensor) -> np.ndarray:
        """LINEAR magnitude mel (not dB) — required for Griffin-Lim"""
        wav_np = wav.squeeze().cpu().numpy()
        return librosa.feature.melspectrogram(
            y=wav_np, sr=self.sr,
            n_fft=self.cfg.n_fft, hop_length=self.cfg.hop_length,
            win_length=self.cfg.win_length, n_mels=self.cfg.n_mels,
            fmin=self.cfg.fmin, fmax=self.cfg.fmax, power=1.0
        )

    def mel_to_db(self, mel_linear: np.ndarray) -> torch.Tensor:
        """Safe dB conversion for model input"""
        mel_db = librosa.amplitude_to_db(mel_linear, ref=np.max)
        return torch.from_numpy(mel_db).float()

    def preemphasis(self, wav: torch.Tensor) -> torch.Tensor:
        c = self.cfg.preemphasis
        return torch.cat([wav[:, :1], wav[:, 1:] - c * wav[:, :-1]], dim=1)

    def get_pitch_yin(self, wav: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        wav_np = wav.squeeze().numpy()
        hop = self.cfg.hop_length
        frame_size = 1024
        f_min, f_max = 80.0, 400.0
        min_lag, max_lag = int(self.sr / f_max), int(self.sr / f_min)
        num_frames = (len(wav_np) - frame_size) // hop + 1
        f0 = np.zeros(num_frames, dtype=np.float32)
        voiced = np.zeros(num_frames, dtype=np.float32)

        def cmnd(frame):
            df = np.array([np.sum((frame[:frame_size-tau] - frame[tau:frame_size])**2) for tau in range(max_lag)])
            cmdf = np.zeros(max_lag); cmdf[0] = 1.0
            cs = 0.0
            for tau in range(1, max_lag):
                cs += df[tau]
                cmdf[tau] = df[tau] * tau / cs if cs > 0 else 1.0
            return cmdf

        for i in range(num_frames):
            frame = wav_np[i*hop: i*hop+frame_size]
            if len(frame) < frame_size: break
            cmdf = cmnd(frame)
            tau = None
            for t in range(min_lag, max_lag):
                if cmdf[t] < 0.12:
                    while t+1 < max_lag and cmdf[t+1] < cmdf[t]: t += 1
                    tau = t; break
            if tau is None: tau = int(np.argmin(cmdf[min_lag:max_lag])) + min_lag
            if cmdf[tau] < 0.3:
                f0[i] = self.sr / max(tau, 1)
                voiced[i] = 1.0

        f0_log = np.where(voiced > 0, np.log(np.maximum(f0, 1e-5)), 0.0)
        return torch.from_numpy(f0_log), torch.from_numpy(voiced)

    def get_energy(self, mel_linear: np.ndarray) -> np.ndarray:
        return np.linalg.norm(mel_linear, axis=0)

    def extract_features(self, wav: torch.Tensor) -> Dict[str, Any]:
        mel_lin = self.wav_to_mel_linear(wav)
        mel_db  = self.mel_to_db(mel_lin)
        f0, voiced = self.get_pitch_yin(wav)
        energy = self.get_energy(mel_lin)
        T = mel_db.shape[1]
        def pad1d(x, tgt):
            x = torch.from_numpy(x).float() if isinstance(x, np.ndarray) else x
            if x.shape[0] > tgt: return x[:tgt]
            return F.pad(x, (0, tgt - x.shape[0]))
        f0 = pad1d(f0, T); voiced = pad1d(voiced, T)
        energy_t = pad1d(torch.from_numpy(energy).float(), T)
        return {"mel_lin": mel_lin, "mel_db": mel_db, "f0": f0,
                "voiced": voiced, "energy": energy_t, "T": T}

    def save_audio(self, wav: torch.Tensor, path: str):
        wav_np = wav.squeeze().cpu().numpy()
        wav_np = np.clip(wav_np, -1.0, 1.0)
        sf.write(path, wav_np, self.sr)

    def pitch_shift(self, wav: torch.Tensor, n_steps: float) -> torch.Tensor:
        if abs(n_steps) < 0.05: return wav
        out = librosa.effects.pitch_shift(wav.squeeze().numpy(), sr=self.sr, n_steps=n_steps)
        return torch.from_numpy(out).float().unsqueeze(0)

    def time_stretch(self, wav: torch.Tensor, rate: float) -> torch.Tensor:
        if abs(rate - 1.0) < 0.02: return wav
        out = librosa.effects.time_stretch(wav.squeeze().numpy(), rate=rate)
        return torch.from_numpy(out).float().unsqueeze(0)

    def generate_demo(self, duration: float = 3.0) -> torch.Tensor:
        """Realistic synthetic voice: glottal model + formant filter"""
        t = np.linspace(0, duration, int(self.sr * duration), endpoint=False)
        f0_c = 120 + 8*np.sin(2*np.pi*0.8*t) + 3*np.sin(2*np.pi*3*t)
        phase = np.cumsum(2*np.pi*f0_c / self.sr)
        glottal = sum(np.sin(k*phase)/(k**1.2) for k in range(1, 14))
        formants = [(700,130),(1220,190),(2600,350),(3500,400)]
        voice = np.zeros_like(glottal)
        for ff, bw in formants:
            b, a = signal.butter(2, [(ff-bw/2)/(self.sr/2),(ff+bw/2)/(self.sr/2)], 'band')
            voice += signal.lfilter(b, a, glottal)
        voice /= (np.abs(voice).max() + 1e-8)
        # Natural on/off envelope
        env = np.ones_like(t)
        env[:int(0.08*self.sr)] = np.linspace(0, 1, int(0.08*self.sr))
        env[-int(0.08*self.sr):] = np.linspace(1, 0, int(0.08*self.sr))
        voice = voice * env * 0.88
        return torch.from_numpy(voice.astype(np.float32)).unsqueeze(0)


