class ResBlock1D(nn.Module):
    def __init__(self, ch, k=3, d=1):
        super().__init__()
        p = (k-1)*d//2
        self.c1 = nn.Conv1d(ch, ch, k, dilation=d, padding=p)
        self.c2 = nn.Conv1d(ch, ch, k, dilation=d, padding=p)
        self.n1 = nn.LayerNorm(ch); self.n2 = nn.LayerNorm(ch)
        self.act = nn.GELU()
    def forward(self, x):
        r = x
        x = self.act(self.c1(self.n1(x.transpose(1,2)).transpose(1,2)))
        x = self.c2(self.n2(x.transpose(1,2)).transpose(1,2))
        return x + r

class VAEEncoder(nn.Module):
    def __init__(self, n_mels, latent_dim, hidden_dim, num_layers):
        super().__init__()
        self.proj = nn.Conv1d(n_mels, hidden_dim, 1)
        self.res  = nn.ModuleList([ResBlock1D(hidden_dim, 3, 2**i) for i in range(num_layers)])
        self.down = nn.Sequential(
            nn.Conv1d(hidden_dim, hidden_dim*2, 4, 2, 1), nn.GELU(),
            nn.Conv1d(hidden_dim*2, hidden_dim*2, 4, 2, 1), nn.GELU(),
            nn.Conv1d(hidden_dim*2, hidden_dim, 4, 2, 1), nn.GELU(),
        )
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc_mu  = nn.Linear(hidden_dim, latent_dim)
        self.fc_lv  = nn.Linear(hidden_dim, latent_dim)
        self.drop   = nn.Dropout(0.1)
    def forward(self, mel):
        x = self.proj(mel)
        for b in self.res: x = b(x)
        x = self.drop(self.down(x))
        x = self.pool(x).squeeze(-1)
        return self.fc_mu(x), torch.clamp(self.fc_lv(x), -10, 4)

class VAEDecoder(nn.Module):
    def __init__(self, n_mels, latent_dim, hidden_dim, num_layers):
        super().__init__()
        self.fc = nn.Linear(latent_dim, hidden_dim * 8)
        self.up = nn.Sequential(
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
        )
        self.res = nn.ModuleList([ResBlock1D(hidden_dim, 3, 2**i) for i in range(num_layers)])
        self.out = nn.Conv1d(hidden_dim, n_mels, 1)
        self.hd  = hidden_dim
    def forward(self, z, T):
        x = self.fc(z).view(z.shape[0], self.hd, 8)
        x = self.up(x)
        for b in self.res: x = b(x)
        x = self.out(x)
        return F.interpolate(x, size=T, mode='linear', align_corners=False)

class VoiceVAE(nn.Module):
    """
    Disentangles speaker identity from content.
    Emotion embedding shifts the latent code.
    Separate predictors for pitch/energy from the conditioned latent.
    """
    def __init__(self, cfg: ModelConfig, n_mels=80):
        super().__init__()
        self.latent_dim = cfg.latent_dim
        self.encoder = VAEEncoder(n_mels, cfg.latent_dim, cfg.vae_hidden_dim, cfg.vae_num_layers)
        self.decoder = VAEDecoder(n_mels, cfg.latent_dim, cfg.vae_hidden_dim, cfg.vae_num_layers)
        self.emo_emb  = nn.Embedding(cfg.num_emotions, cfg.latent_dim)
        nn.init.normal_(self.emo_emb.weight, 0, 0.1)
        self.emo_map  = nn.Sequential(
            nn.Linear(cfg.latent_dim*2, cfg.latent_dim*2), nn.GELU(),
            nn.Linear(cfg.latent_dim*2, cfg.latent_dim), nn.Tanh()
        )
        self.pitch_head  = nn.Sequential(nn.Linear(cfg.latent_dim, cfg.vae_hidden_dim), nn.GELU(), nn.Linear(cfg.vae_hidden_dim, 1))
        self.energy_head = nn.Sequential(nn.Linear(cfg.latent_dim, cfg.vae_hidden_dim), nn.GELU(), nn.Linear(cfg.vae_hidden_dim, 1))

    def encode(self, mel): return self.encoder(mel)

    def reparameterize(self, mu, lv):
        if self.training:
            return mu + torch.randn_like(mu) * torch.exp(0.5*lv)
        return mu

    def condition(self, z, emo_id):
        e = self.emo_emb(emo_id)
        return self.emo_map(torch.cat([z, e], -1))

    def forward(self, mel, emo_id=None):
        B, _, T = mel.shape
        mu, lv = self.encode(mel)
        z = self.reparameterize(mu, lv)
        zc = self.condition(z, emo_id) if emo_id is not None else z
        recon = self.decoder(zc, T)
        return {"recon": recon, "mu": mu, "log_var": lv, "z": z, "z_cond": zc,
                "pitch_pred": self.pitch_head(zc).squeeze(-1),
                "energy_pred": self.energy_head(zc).squeeze(-1)}


