class GriffinLimVocoder:
    """
    Correct Griffin-Lim: inverts the mel filterbank via pseudoinverse,
    then runs phase recovery. Input must be LINEAR magnitude mel, not dB.
    """
    def __init__(self, cfg: AudioConfig, n_iter=80):
        self.cfg = cfg
        self.n_iter = n_iter
        fb = librosa.filters.mel(sr=cfg.sample_rate, n_fft=cfg.n_fft,
                                  n_mels=cfg.n_mels, fmin=cfg.fmin, fmax=cfg.fmax)
        self.fb_inv = np.linalg.pinv(fb)

    def __call__(self, mel_linear: np.ndarray) -> torch.Tensor:
        stft_mag = np.maximum(0, self.fb_inv @ mel_linear)
        ang = np.exp(1j * np.random.uniform(0, 2*np.pi, stft_mag.shape))
        for i in range(self.n_iter):
            wav = librosa.istft(stft_mag * ang, hop_length=self.cfg.hop_length,
                                win_length=self.cfg.win_length, n_fft=self.cfg.n_fft)
            if i < self.n_iter - 1:
                stft = librosa.stft(wav, n_fft=self.cfg.n_fft,
                                    hop_length=self.cfg.hop_length, win_length=self.cfg.win_length)
                ang  = np.exp(1j * np.angle(stft))
        return torch.from_numpy(wav.astype(np.float32)).unsqueeze(0)


