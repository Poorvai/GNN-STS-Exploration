class HiFiResBlock(nn.Module):
    def __init__(self, ch, k=3, dilations=(1,3,5)):
        super().__init__()
        def wn(m): return nn.utils.weight_norm(m)
        p = lambda k,d: (k*d-d)//2
        self.c1 = nn.ModuleList([wn(nn.Conv1d(ch,ch,k,dilation=d,padding=p(k,d))) for d in dilations])
        self.c2 = nn.ModuleList([wn(nn.Conv1d(ch,ch,k,dilation=1,padding=p(k,1))) for _ in dilations])
    def forward(self, x):
        for a, b in zip(self.c1, self.c2):
            xt = F.leaky_relu(x, 0.1)
            xt = F.leaky_relu(a(xt), 0.1)
            xt = b(xt)
            x = x + xt
        return x

class HiFiGANGenerator(nn.Module):
    """
    HiFi-GAN V1 generator.
    Produces 256×hop_length upsampling via transposed convolutions.
    Multi-receptive field fusion (MRF) captures all speech timescales.
    Output passes through tanh so values are in [-1, 1] — no beeping.
    """
    def __init__(self, cfg: ModelConfig, n_mels=80):
        super().__init__()
        wn = nn.utils.weight_norm
        self.pre = wn(nn.Conv1d(n_mels, cfg.hifigan_upsample_initial_channel, 7, 1, 3))
        self.ups, self.blocks = nn.ModuleList(), nn.ModuleList()
        ch = cfg.hifigan_upsample_initial_channel
        for u, k in zip(cfg.hifigan_upsample_rates, cfg.hifigan_upsample_kernel_sizes):
            self.ups.append(wn(nn.ConvTranspose1d(ch, ch//2, k, u, (k-u)//2)))
            for rk, rd in zip(cfg.hifigan_resblock_kernel_sizes, cfg.hifigan_resblock_dilation_sizes):
                self.blocks.append(HiFiResBlock(ch//2, rk, rd))
            ch //= 2
        self.post = wn(nn.Conv1d(ch, 1, 7, 1, 3))
        self.nk = len(cfg.hifigan_resblock_kernel_sizes)
        self.nu = len(cfg.hifigan_upsample_rates)

    def forward(self, mel):
        x = self.pre(mel)
        for i in range(self.nu):
            x = F.leaky_relu(x, 0.1)
            x = self.ups[i](x)
            xs = None
            for j in range(self.nk):
                y = self.blocks[i*self.nk+j](x)
                xs = y if xs is None else xs + y
            x = xs / self.nk
        return torch.tanh(self.post(F.leaky_relu(x, 0.1)))


