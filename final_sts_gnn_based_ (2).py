# ║  CELL 6 ★ NEW — STFT SPECTRAL FINGERPRINT ANALYSIS                     ║
# ║                                                                          ║
# ║  What it does:                                                           ║
# ║    Computes per-emotion Short-Time Fourier Transform (STFT) and         ║
# ║    extracts five spectral descriptors that characterise the frequency    ║
# ║    signature of each emotion's output:                                   ║
# ║      • Spectral Centroid (brightness / "centre of mass" of spectrum)     ║
# ║      • Spectral Bandwidth (spread of energy around centroid)             ║
# ║      • Spectral Rolloff (frequency below which 85% of energy lies)      ║
# ║      • Spectral Flatness (tonality vs noise-like quality)               ║
# ║      • Zero Crossing Rate (per-frame sign changes → roughness)          ║
# ║    It also shows the STFT magnitude heatmap per emotion side-by-side.    ║
# ║                                                                          ║
# ║  Why it matters:                                                         ║
# ║    Spectral descriptors are the acoustic correlates that human           ║
# ║    perception uses to distinguish emotions without language content.     ║
# ║    High centroid = bright/happy; low rolloff = dark/sad. Analysing       ║
# ║    these proves the pipeline is making acoustically correct changes.     ║
# ╚══════════════════════════════════════════════════════════════════════════╝

def stft_spectral_analysis(results: Dict, audio_cfg: AudioConfig) -> pd.DataFrame:
    """
    Compute spectral fingerprints for every converted emotion audio.

    Parameters
    ──────────
    results    : dict {emotion: {"wav": Tensor, "path": str}} from Cell 3
    audio_cfg  : AudioConfig

    Returns
    ───────
    pd.DataFrame with one row per emotion and 5 spectral descriptor columns
    """
    import pandas as pd

    sr    = audio_cfg.sample_rate
    n_fft = audio_cfg.n_fft
    hop   = audio_cfg.hop_length

    descriptors = []
    stft_mags   = {}

    for emo, data in tqdm(results.items(), desc="STFT Analysis",
                          bar_format="{l_bar}{bar}| {n_fmt}/{total_fmt}"):
        w = data["wav"].squeeze().cpu().numpy().astype(np.float32)

        stft     = np.abs(librosa.stft(w, n_fft=n_fft, hop_length=hop))
        stft_mags[emo] = stft[:64, :]    # Store lower 64 bins for display

        centroid  = librosa.feature.spectral_centroid(y=w,  sr=sr, n_fft=n_fft, hop_length=hop)
        bandwidth = librosa.feature.spectral_bandwidth(y=w,  sr=sr, n_fft=n_fft, hop_length=hop)
        rolloff   = librosa.feature.spectral_rolloff(y=w,   sr=sr, n_fft=n_fft, hop_length=hop, roll_percent=0.85)
        flatness  = librosa.feature.spectral_flatness(y=w,  n_fft=n_fft, hop_length=hop)
        zcr       = librosa.feature.zero_crossing_rate(w, hop_length=hop)

        descriptors.append({
            "Emotion":             emo.upper(),
            "Centroid (Hz)":       float(centroid.mean()),
            "Bandwidth (Hz)":      float(bandwidth.mean()),
            "Rolloff (Hz)":        float(rolloff.mean()),
            "Flatness":            float(flatness.mean()),
            "ZCR":                 float(zcr.mean()),
            "STFT Energy (dB)":   float(20 * np.log10(stft.mean() + 1e-8)),
        })

    df = pd.DataFrame(descriptors)

    # ── Visualisation ──────────────────────────────────────────────────────
    n_emo = len(results)
