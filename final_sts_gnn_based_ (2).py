# GRAPH 5: Emotion similarity matrix + latent space t-SNE
# ─────────────────────────────────────────────────────────────────────────────
def plot_similarity_and_latent():
    fig, axes = plt.subplots(1, 2, figsize=(20, 8))
    fig.suptitle("EMOTION SIMILARITY MATRIX & LATENT SPACE (GNN Outputs)",
                 fontsize=14, fontweight="bold", color="#58a6ff")

    # ── Similarity matrix ────────────────────────────────────────────────────
    ax1 = axes[0]
    sim_mat = np.zeros((len(EMO_LIST), len(EMO_LIST)))
    for i, e1 in enumerate(EMO_LIST):
        for j, e2 in enumerate(EMO_LIST):
            v1, a1 = VA_POS[e1]; v2, a2 = VA_POS[e2]
            d = np.sqrt((v1-v2)**2 + (a1-a2)**2)
            sim_mat[i, j] = 1.0 - d / np.sqrt(2.0)

    mask = np.eye(len(EMO_LIST), dtype=bool)
    cmap_sim = LinearSegmentedColormap.from_list("sim", ["#0d1117","#1a3a5c","#2563eb","#60a5fa","#bae6fd"])
    im = ax1.imshow(sim_mat, cmap=cmap_sim, vmin=0, vmax=1)
    ax1.set_xticks(range(len(EMO_LIST))); ax1.set_xticklabels([e.upper() for e in EMO_LIST], rotation=35, ha='right', fontsize=9)
    ax1.set_yticks(range(len(EMO_LIST))); ax1.set_yticklabels([e.upper() for e in EMO_LIST], fontsize=9)
    ax1.set_title("VA-Space Similarity Matrix\n(determines graph edge weights)", color="#79c0ff", fontsize=11)
    for i in range(len(EMO_LIST)):
        for j in range(len(EMO_LIST)):
            ax1.text(j, i, f"{sim_mat[i,j]:.2f}", ha='center', va='center',
                     fontsize=8, color='white' if sim_mat[i,j] < 0.65 else '#0d1117')
    fig.colorbar(im, ax=ax1, shrink=0.8)

    # Colour-coded row/col labels
    for tick, emo in zip(ax1.get_xticklabels(), EMO_LIST):
        tick.set_color(COLORS[emo])
    for tick, emo in zip(ax1.get_yticklabels(), EMO_LIST):
        tick.set_color(COLORS[emo])

    # ── GNN output latent distribution ──────────────────────────────────────
    ax2 = axes[1]
    model.eval()
    device = next(model.parameters()).device
    z_samples = {}
    N = 30   # samples per emotion
    np.random.seed(42)
    for tgt_emo in EMO_LIST:
        vecs = []
        for _ in range(N):
            z_d = torch.randn(1, MODEL_CFG.latent_dim, device=device)
            sid = torch.tensor([EMOTION_IDX["neutral"]], device=device)
            tid = torch.tensor([EMOTION_IDX[tgt_emo]], device=device)
            with torch.no_grad():
                out = model.gnn(z_d, sid, tid)
            vecs.append(out["z_target"].cpu().squeeze().numpy())
        z_samples[tgt_emo] = np.stack(vecs)

    all_z = np.concatenate([z_samples[e] for e in EMO_LIST])
    all_labels = sum([[i]*N for i, _ in enumerate(EMO_LIST)], [])

    # PCA (fast substitute for t-SNE in Colab for instant rendering)
    from numpy.linalg import svd
    z_c = all_z - all_z.mean(0)
    U, S, Vt = svd(z_c, full_matrices=False)
    pca_coords = z_c @ Vt[:2].T   # project to 2D

    for i, emo in enumerate(EMO_LIST):
        idx = [j for j, l in enumerate(all_labels) if l == i]
        xy  = pca_coords[idx]
        ax2.scatter(xy[:,0], xy[:,1], c=COLORS[emo], s=60, alpha=0.7,
                    edgecolors="#30363d", linewidths=0.5, label=emo.upper())
        cx, cy = xy.mean(0)
        ax2.text(cx, cy, emo[:3].upper(), ha='center', va='center',
                 fontsize=10, color=COLORS[emo], fontweight='bold',
                 path_effects=[pe.withStroke(linewidth=3, foreground='#0d1117')])

    ax2.set_title("GNN Output Latent Space\n(PCA of z_target per emotion, 30 samples each)",
                  color="#79c0ff", fontsize=11)
    ax2.set_xlabel("PC 1"); ax2.set_ylabel("PC 2")
    ax2.legend(loc="upper right", fontsize=7, framealpha=0.3,
               labelcolor="white", facecolor="#161b22", edgecolor="#30363d")

    plt.tight_layout(rect=[0, 0, 1, 0.94])
    _show(fig)
    print("Graph 5: Similarity matrix and latent space rendered.")


