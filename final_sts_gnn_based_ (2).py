# ─── AudioProcessor ──────────────────────────────────────────────────────────
class AudioProcessor:
    def __init__(self, cfg: AudioConfig):
        self.cfg = cfg
        self.sr  = cfg.sample_rate

    def load_audio(self, path: str) -> torch.Tensor:
        try:
            sr_file, data = wav_io.read(path)
            if data.dtype == np.int16:   data = data.astype(np.float32) / 32768.0
            elif data.dtype == np.int32: data = data.astype(np.float32) / 2147483648.0
            else:                        data = data.astype(np.float32)
            if data.ndim == 2: data = data.mean(axis=1)
            wav = torch.from_numpy(data).unsqueeze(0)
            if sr_file != self.sr:
                wav = T.Resample(sr_file, self.sr)(wav)
        except Exception:
            wav, sr_file = torchaudio.load(path)
            if sr_file != self.sr:
                wav = T.Resample(sr_file, self.sr)(wav)
        if wav.shape[0] > 1: wav = wav.mean(0, keepdim=True)
        wav = wav / (wav.abs().max() + 1e-8)
        return wav

    def wav_to_mel_linear(self, wav: torch.Tensor) -> np.ndarray:
        """LINEAR magnitude mel — required for Griffin-Lim and correct energy scaling."""
        wav_np = wav.squeeze().cpu().numpy()
        return librosa.feature.melspectrogram(
            y=wav_np, sr=self.sr,
            n_fft=self.cfg.n_fft, hop_length=self.cfg.hop_length,
            win_length=self.cfg.win_length, n_mels=self.cfg.n_mels,
            fmin=self.cfg.fmin, fmax=self.cfg.fmax, power=1.0,
        )

    def mel_to_db(self, mel_linear: np.ndarray) -> torch.Tensor:
        mel_db = librosa.amplitude_to_db(mel_linear, ref=np.max)
        return torch.from_numpy(mel_db).float()

    def get_pitch_yin(self, wav: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        wav_np   = wav.squeeze().numpy()
        hop      = self.cfg.hop_length
        f_min, f_max = 80.0, 400.0
        min_lag  = int(self.sr / f_max)
        max_lag  = int(self.sr / f_min)
        fr_size  = 1024
        n_frames = (len(wav_np) - fr_size) // hop + 1
        f0       = np.zeros(n_frames, dtype=np.float32)
        voiced   = np.zeros(n_frames, dtype=np.float32)

        def cumulative_mean_normalised_difference(frame):
            df = np.array([
                np.sum((frame[:fr_size - tau] - frame[tau:fr_size]) ** 2)
                for tau in range(max_lag)
            ])
            cmdf = np.zeros(max_lag); cmdf[0] = 1.0
            cs = 0.0
            for tau in range(1, max_lag):
                cs += df[tau]
                cmdf[tau] = df[tau] * tau / cs if cs > 0 else 1.0
            return cmdf

        for i in range(n_frames):
            frame = wav_np[i * hop: i * hop + fr_size]
            if len(frame) < fr_size: break
            cmdf  = cumulative_mean_normalised_difference(frame)
            tau   = None
            for t in range(min_lag, max_lag):
                if cmdf[t] < 0.12:
                    while t + 1 < max_lag and cmdf[t + 1] < cmdf[t]: t += 1
                    tau = t; break
            if tau is None: tau = int(np.argmin(cmdf[min_lag:max_lag])) + min_lag
            if cmdf[tau] < 0.3:
                f0[i]     = self.sr / max(tau, 1)
                voiced[i] = 1.0

        f0_log = np.where(voiced > 0, np.log(np.maximum(f0, 1e-5)), 0.0)
        return torch.from_numpy(f0_log), torch.from_numpy(voiced)

    def get_energy(self, mel_linear: np.ndarray) -> np.ndarray:
        return np.linalg.norm(mel_linear, axis=0)

    def extract_features(self, wav: torch.Tensor) -> Dict[str, Any]:
        mel_lin     = self.wav_to_mel_linear(wav)
        mel_db      = self.mel_to_db(mel_lin)
        f0, voiced  = self.get_pitch_yin(wav)
        energy      = self.get_energy(mel_lin)
        T_frames    = mel_db.shape[1]

        def pad1d(x, tgt):
            x = torch.from_numpy(x).float() if isinstance(x, np.ndarray) else x
            return x[:tgt] if x.shape[0] > tgt else F.pad(x, (0, tgt - x.shape[0]))

        f0     = pad1d(f0, T_frames)
        voiced = pad1d(voiced, T_frames)
        energy = pad1d(torch.from_numpy(energy).float(), T_frames)
        return {"mel_lin": mel_lin, "mel_db": mel_db,
                "f0": f0, "voiced": voiced, "energy": energy, "T": T_frames}

    def save_audio(self, wav: torch.Tensor, path: str):
        wav_np = np.clip(wav.squeeze().cpu().numpy(), -1.0, 1.0)
        sf.write(path, wav_np, self.sr)

    def pitch_shift(self, wav: torch.Tensor, n_steps: float) -> torch.Tensor:
        if abs(n_steps) < 0.05: return wav
        out = librosa.effects.pitch_shift(wav.squeeze().numpy(), sr=self.sr, n_steps=n_steps)
        return torch.from_numpy(out).float().unsqueeze(0)

    def time_stretch(self, wav: torch.Tensor, rate: float) -> torch.Tensor:
        if abs(rate - 1.0) < 0.02: return wav
        out = librosa.effects.time_stretch(wav.squeeze().numpy(), rate=rate)
        return torch.from_numpy(out).float().unsqueeze(0)

    def generate_demo(self, duration: float = 3.0) -> torch.Tensor:
        """Realistic synthetic voice via glottal + formant model."""
        t       = np.linspace(0, duration, int(self.sr * duration), endpoint=False)
        f0_c    = 120 + 8 * np.sin(2 * np.pi * 0.8 * t) + 3 * np.sin(2 * np.pi * 3 * t)
        phase   = np.cumsum(2 * np.pi * f0_c / self.sr)
        glottal = sum(np.sin(k * phase) / (k ** 1.2) for k in range(1, 14))
        formants = [(700, 130), (1220, 190), (2600, 350), (3500, 400)]
        voice = np.zeros_like(glottal)
        for ff, bw in formants:
            b, a  = signal.butter(2, [(ff - bw / 2) / (self.sr / 2), (ff + bw / 2) / (self.sr / 2)], "band")
            voice += signal.lfilter(b, a, glottal)
        voice /= np.abs(voice).max() + 1e-8
        env    = np.ones_like(t)
        fade   = int(0.08 * self.sr)
        env[:fade]  = np.linspace(0, 1, fade)
        env[-fade:] = np.linspace(1, 0, fade)
        return torch.from_numpy((voice * env * 0.88).astype(np.float32)).unsqueeze(0)


# ─── VAE ─────────────────────────────────────────────────────────────────────
class ResBlock1D(nn.Module):
    def __init__(self, ch, k=3, d=1):
        super().__init__()
        p = (k - 1) * d // 2
        self.c1 = nn.Conv1d(ch, ch, k, dilation=d, padding=p)
        self.c2 = nn.Conv1d(ch, ch, k, dilation=d, padding=p)
        self.n1 = nn.LayerNorm(ch); self.n2 = nn.LayerNorm(ch)
        self.act = nn.GELU()
    def forward(self, x):
        r = x
        x = self.act(self.c1(self.n1(x.transpose(1, 2)).transpose(1, 2)))
        x = self.c2(self.n2(x.transpose(1, 2)).transpose(1, 2))
        return x + r

class VAEEncoder(nn.Module):
    def __init__(self, n_mels, latent_dim, hidden_dim, num_layers):
        super().__init__()
        self.proj = nn.Conv1d(n_mels, hidden_dim, 1)
        self.res  = nn.ModuleList([ResBlock1D(hidden_dim, 3, 2 ** i) for i in range(num_layers)])
        self.down = nn.Sequential(
            nn.Conv1d(hidden_dim, hidden_dim * 2, 4, 2, 1), nn.GELU(),
            nn.Conv1d(hidden_dim * 2, hidden_dim * 2, 4, 2, 1), nn.GELU(),
            nn.Conv1d(hidden_dim * 2, hidden_dim, 4, 2, 1), nn.GELU(),
        )
        self.pool   = nn.AdaptiveAvgPool1d(1)
        self.fc_mu  = nn.Linear(hidden_dim, latent_dim)
        self.fc_lv  = nn.Linear(hidden_dim, latent_dim)
        self.drop   = nn.Dropout(0.1)
    def forward(self, mel):
        x = self.proj(mel)
        for b in self.res: x = b(x)
        x = self.drop(self.down(x))
        x = self.pool(x).squeeze(-1)
        return self.fc_mu(x), torch.clamp(self.fc_lv(x), -10, 4)

class VAEDecoder(nn.Module):
    def __init__(self, n_mels, latent_dim, hidden_dim, num_layers):
        super().__init__()
        self.fc = nn.Linear(latent_dim, hidden_dim * 8)
        self.up = nn.Sequential(
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
            nn.ConvTranspose1d(hidden_dim, hidden_dim, 4, 2, 1), nn.GELU(),
        )
        self.res = nn.ModuleList([ResBlock1D(hidden_dim, 3, 2 ** i) for i in range(num_layers)])
        self.out = nn.Conv1d(hidden_dim, n_mels, 1)
        self.hd  = hidden_dim
    def forward(self, z, T):
        x = self.fc(z).view(z.shape[0], self.hd, 8)
        x = self.up(x)
        for b in self.res: x = b(x)
        x = self.out(x)
        return F.interpolate(x, size=T, mode="linear", align_corners=False)

class VoiceVAE(nn.Module):
    """
    Disentangles speaker identity from prosody via a β-VAE objective.

    Input  : Mel spectrogram  [B, n_mels, T]   (range: dB, typically -80…0)
    Latent : z ∈ R^128        (unit-Gaussian prior after reparameterisation)
    Output : reconstructed mel + conditioned latent z_cond

    Key design choices:
      • Emotion embedding (7→128) is concatenated with z, mapped through
        a 2-layer MLP to obtain z_cond.  This keeps speaker identity in z
        while emotion characteristics live in the residual z_cond − z.
      • Separate pitch/energy predictors from z_cond so FastSpeech2 can
        use them as soft targets without an explicit phoneme lexicon.
    """
    def __init__(self, cfg: ModelConfig, n_mels: int = 80):
        super().__init__()
        self.latent_dim = cfg.latent_dim
        self.encoder  = VAEEncoder(n_mels, cfg.latent_dim, cfg.vae_hidden_dim, cfg.vae_num_layers)
        self.decoder  = VAEDecoder(n_mels, cfg.latent_dim, cfg.vae_hidden_dim, cfg.vae_num_layers)
        self.emo_emb  = nn.Embedding(cfg.num_emotions, cfg.latent_dim)
        nn.init.normal_(self.emo_emb.weight, 0, 0.1)
        self.emo_map  = nn.Sequential(
            nn.Linear(cfg.latent_dim * 2, cfg.latent_dim * 2), nn.GELU(),
            nn.Linear(cfg.latent_dim * 2, cfg.latent_dim), nn.Tanh(),
        )
        self.pitch_head  = nn.Sequential(
            nn.Linear(cfg.latent_dim, cfg.vae_hidden_dim), nn.GELU(),
            nn.Linear(cfg.vae_hidden_dim, 1),
        )
        self.energy_head = nn.Sequential(
            nn.Linear(cfg.latent_dim, cfg.vae_hidden_dim), nn.GELU(),
            nn.Linear(cfg.vae_hidden_dim, 1),
        )

    def encode(self, mel): return self.encoder(mel)

    def reparameterize(self, mu, lv):
        if self.training:
            return mu + torch.randn_like(mu) * torch.exp(0.5 * lv)
        return mu

    def condition(self, z, emo_id):
        e = self.emo_emb(emo_id)
        return self.emo_map(torch.cat([z, e], -1))

    def forward(self, mel, emo_id=None):
        B, _, T = mel.shape
        mu, lv  = self.encode(mel)
        z       = self.reparameterize(mu, lv)
        z_cond  = self.condition(z, emo_id) if emo_id is not None else z
        recon   = self.decoder(z_cond, T)
        return {
            "recon": recon, "mu": mu, "log_var": lv, "z": z, "z_cond": z_cond,
            "pitch_pred":  self.pitch_head(z_cond).squeeze(-1),
            "energy_pred": self.energy_head(z_cond).squeeze(-1),
        }


# ─── EmotionGNN ───────────────────────────────────────────────────────────────
class GATLayer(nn.Module):
    """
    Graph Attention Network layer.

    Input  : node features  [N, in_dim],  edge_index [2, E]
    Output : updated nodes  [N, out_dim]
    Range  : activations bounded by LayerNorm + residual; attention ∈ (0,1]

    Attention mechanism:
        e_ij  = LeakyReLU( a^T [h_i ‖ h_j] )
        α_ij  = softmax_j( e_ij )
        h'_i  = Σ_j α_ij W h_j

    Weights are saved in `last_attn_weights` for visualisation after each forward.
    """
    def __init__(self, in_dim, out_dim, n_heads, dropout=0.1):
        super().__init__()
        self.n_heads  = n_heads
        self.head_dim = out_dim // n_heads
        self.Wq = nn.Linear(in_dim, out_dim)
        self.Wk = nn.Linear(in_dim, out_dim)
        self.Wv = nn.Linear(in_dim, out_dim)
        self.Wo = nn.Linear(out_dim, out_dim)
        self.attn_a = nn.Parameter(torch.randn(n_heads, self.head_dim * 2))
        nn.init.xavier_uniform_(self.attn_a.unsqueeze(0))
        self.lrelu  = nn.LeakyReLU(0.2)
        self.drop   = nn.Dropout(dropout)
        self.norm   = nn.LayerNorm(out_dim)
        self.last_attn_weights: Optional[torch.Tensor] = None

    def forward(self, x, edge_index, edge_weight=None):
        N = x.shape[0]
        Q = self.Wq(x).view(N, self.n_heads, self.head_dim)
        K = self.Wk(x).view(N, self.n_heads, self.head_dim)
        V = self.Wv(x).view(N, self.n_heads, self.head_dim)
        src, tgt = edge_index[0], edge_index[1]
        ai = torch.cat([Q[src], K[tgt]], -1)
        e  = self.lrelu((ai * self.attn_a.unsqueeze(0)).sum(-1))
        if edge_weight is not None: e = e * edge_weight.unsqueeze(-1)
        exp_e    = torch.exp(e - e.max())
        norm_sum = torch.zeros(N, self.n_heads, device=x.device)
        norm_sum.index_add_(0, tgt, exp_e)
        alpha    = exp_e / (norm_sum[tgt] + 1e-8)
        self.last_attn_weights = alpha.detach().mean(-1)
        alpha    = self.drop(alpha)
        agg      = torch.zeros(N, self.n_heads, self.head_dim, device=x.device)
        agg.index_add_(0, tgt, V[src] * alpha.unsqueeze(-1))
        out = self.Wo(agg.view(N, -1))
        return self.norm(out + x if out.shape == x.shape else out)


class EmotionGNN(nn.Module):
    """
    7-node GAT over the Valence-Arousal emotion graph.

    Node features : z_source (128-d) concatenated with 6 prosody scalars
    Edge weights  : 1 − normalised VA-space Euclidean distance
    Layers        : 4 × GATLayer (each with 4 attention heads)

    Input dims  : node_dim = latent_dim + 6 = 134
    Hidden dims : 256 (gnn_hidden_dim)
    Output      : z_target ∈ R^128, prosody_target ∈ R^6
    """
    def __init__(self, cfg: ModelConfig):
        super().__init__()
        self.cfg      = cfg
        self.node_dim = cfg.latent_dim + 6
        self.hd       = cfg.gnn_hidden_dim

        self.node_enc = nn.Sequential(
            nn.Linear(self.node_dim, self.hd), nn.GELU(),
            nn.Dropout(cfg.dropout),
            nn.Linear(self.hd, self.hd),
        )
        self.gat_layers = nn.ModuleList([
            GATLayer(self.hd, self.hd, cfg.gnn_num_heads, cfg.dropout)
            for _ in range(cfg.gnn_num_layers)
        ])
        self.ffn_layers = nn.ModuleList([
            nn.Sequential(
                nn.Linear(self.hd, self.hd * 2), nn.GELU(),
                nn.Dropout(cfg.dropout),
                nn.Linear(self.hd * 2, self.hd),
            ) for _ in range(cfg.gnn_num_layers)
        ])
        self.norms = nn.ModuleList([nn.LayerNorm(self.hd) for _ in range(cfg.gnn_num_layers)])
        self.out   = nn.Sequential(
            nn.Linear(self.hd, self.hd), nn.GELU(),
            nn.Linear(self.hd, cfg.latent_dim), nn.Tanh(),
        )
        self.prosody_dec = nn.Sequential(
            nn.Linear(cfg.latent_dim, self.hd), nn.GELU(),
            nn.Linear(self.hd, 6),
        )
        self.base_nodes = nn.Parameter(torch.randn(cfg.num_emotions, self.node_dim) * 0.1)

        ei, ew = self._build_graph()
        self.register_buffer("edge_index",  ei)
        self.register_buffer("edge_weight", ew)

        self.last_attention_per_layer: List[List[torch.Tensor]] = []
        self.last_node_activations: Optional[torch.Tensor]      = None

    def _build_graph(self):
        src_list, tgt_list, w_list = [], [], []
        emos = list(EMOTION_PROFILES.keys())
        for i, e1 in enumerate(emos):
            for j, e2 in enumerate(emos):
                if i != j:
                    v1, a1 = VA_POS[e1]; v2, a2 = VA_POS[e2]
                    d   = math.sqrt((v1 - v2) ** 2 + (a1 - a2) ** 2)
                    sim = 1.0 - d / math.sqrt(2.0)
                    src_list.append(i); tgt_list.append(j); w_list.append(sim)
        return (torch.tensor([src_list, tgt_list], dtype=torch.long),
                torch.tensor(w_list, dtype=torch.float32))

    def forward(self, z_src, src_id, tgt_id, prosody=None):
        B      = z_src.shape[0]
        device = z_src.device
        if prosody is None: prosody = torch.zeros(B, 6, device=device)
        ei, ew = self.edge_index, self.edge_weight
        results = []
        self.last_attention_per_layer = []

        for b in range(B):
            nodes = self.base_nodes.clone()
            feat  = torch.cat([z_src[b:b+1], prosody[b:b+1]], -1)
            nodes[src_id[b].item()] = nodes[src_id[b].item()] + feat.squeeze(0)
            x = self.node_enc(nodes)
            attn_batch = []
            for li in range(len(self.gat_layers)):
                xa = self.gat_layers[li](x, ei, ew)
                attn_batch.append(self.gat_layers[li].last_attn_weights.cpu())
                x  = self.norms[li](self.ffn_layers[li](xa) + x)
            self.last_attention_per_layer.append(attn_batch)
            self.last_node_activations = x.detach().cpu()
            results.append(self.out(x[tgt_id[b].item()].unsqueeze(0)))

        z_out = torch.cat(results, 0)
        return {"z_target": z_out, "prosody_target": self.prosody_dec(z_out)}


# ─── FastSpeech2 Variance Adaptor ────────────────────────────────────────────
class SinPositionalEncoding(nn.Module):
    def __init__(self, d, maxlen=5000, drop=0.1):
        super().__init__()
        self.drop = nn.Dropout(drop)
        pe  = torch.zeros(maxlen, d)
        pos = torch.arange(0, maxlen).float().unsqueeze(1)
        div = torch.exp(torch.arange(0, d, 2).float() * (-math.log(10000.0) / d))
        pe[:, 0::2] = torch.sin(pos * div)
        pe[:, 1::2] = torch.cos(pos * div)
        self.register_buffer("pe", pe.unsqueeze(0))
    def forward(self, x): return self.drop(x + self.pe[:, :x.size(1)])

class FFTBlock(nn.Module):
    """Feed-Forward Transformer block (self-attention + 1D-conv FFN)."""
    def __init__(self, d, heads, d_ff, k=9, drop=0.1):
        super().__init__()
        self.attn = nn.MultiheadAttention(d, heads, dropout=drop, batch_first=True)
        self.n1   = nn.LayerNorm(d); self.n2 = nn.LayerNorm(d)
        p = (k - 1) // 2
        self.ffn  = nn.Sequential(
            nn.Conv1d(d, d_ff, k, padding=p), nn.GELU(), nn.Dropout(drop),
            nn.Conv1d(d_ff, d, k, padding=p),
        )
        self.drop = nn.Dropout(drop)
    def forward(self, x, mask=None):
        a, _ = self.attn(x, x, x, key_padding_mask=mask)
        x    = self.n1(x + self.drop(a))
        f    = self.ffn(x.transpose(1, 2)).transpose(1, 2)
        return self.n2(x + self.drop(f))

class VariancePredictor(nn.Module):
    def __init__(self, d, out=1, k=3):
        super().__init__()
        p = (k - 1) // 2
        self.net = nn.Sequential(
            nn.Conv1d(d, d, k, padding=p), nn.GELU(), nn.LayerNorm(d),
            nn.Conv1d(d, d, k, padding=p), nn.GELU(), nn.LayerNorm(d),
            nn.Linear(d, out),
        )
    def forward(self, x):
        h = x
        for layer in self.net:
            if isinstance(layer, nn.Conv1d):      h = layer(h.transpose(1, 2)).transpose(1, 2)
            elif isinstance(layer, nn.LayerNorm): h = layer(h)
            else:                                  h = layer(h)
        return h

class PitchEmbedding(nn.Module):
    def __init__(self, bins, d, f0min=-3.0, f0max=3.0):
        super().__init__()
        self.emb  = nn.Embedding(bins + 1, d)
        self.bins = bins
        self.register_buffer("b", torch.linspace(f0min, f0max, bins))
    def forward(self, f0):
        idx = torch.bucketize(f0.clamp(self.b[0], self.b[-1]), self.b).clamp(0, self.bins)
        return self.emb(idx)

class EnergyEmbedding(nn.Module):
    def __init__(self, bins, d, emin=0.0, emax=150.0):
        super().__init__()
        self.emb  = nn.Embedding(bins + 1, d)
        self.bins = bins
        self.register_buffer("b", torch.linspace(emin, emax, bins))
    def forward(self, e):
        idx = torch.bucketize(e.clamp(self.b[0], self.b[-1]), self.b).clamp(0, self.bins)
        return self.emb(idx)

class LengthRegulator(nn.Module):
    """Expands phoneme-level hidden states to frame-level by repeating according to durations."""
    def forward(self, x, dur, T):
        out = []
        for b in range(x.shape[0]):
            d   = dur[b].clamp(min=1).long()
            d   = (d.float() * T / d.sum().clamp(1)).round().clamp(1).long()
            rep = torch.repeat_interleave(x[b], d, dim=0)
            rep = rep[:T] if rep.shape[0] >= T else F.pad(rep, (0, 0, 0, T - rep.shape[0]))
            out.append(rep)
        return torch.stack(out, 0)

class FastSpeech2VarianceAdaptor(nn.Module):
    """
    Controls pitch, energy, duration.

    Input  : (z_content [B,128], z_emotion [B,128])
    Output : mel spectrogram [B, n_mels, T], pitch_pred, energy_pred, dur_pred

    Gating mechanism:
        gate = σ( W [h_content ‖ h_emotion] )
        h    = h_content × gate + h_emotion × (1 − gate)
    This creates a soft mixture that never fully discards content.
    """
    def __init__(self, cfg: ModelConfig, n_mels: int = 80):
        super().__init__()
        d = cfg.fastspeech_hidden
        self.inp    = nn.Linear(cfg.latent_dim, d)
        self.pe     = SinPositionalEncoding(d, drop=cfg.dropout)
        self.ph_enc = nn.ModuleList([
            FFTBlock(d, cfg.fastspeech_num_heads, cfg.fastspeech_filter_size,
                     cfg.fastspeech_kernel_size, cfg.dropout)
            for _ in range(cfg.fastspeech_num_layers)
        ])
        self.dur_p  = VariancePredictor(d)
        self.len_r  = LengthRegulator()
        self.pit_p  = VariancePredictor(d)
        self.ene_p  = VariancePredictor(d)
        self.pit_e  = PitchEmbedding(cfg.pitch_bins, d)
        self.ene_e  = EnergyEmbedding(cfg.energy_bins, d)
        self.emo_p  = nn.Linear(cfg.latent_dim, d)
        self.gate   = nn.Sequential(nn.Linear(d * 2, d), nn.Sigmoid())
        self.me_enc = nn.ModuleList([
            FFTBlock(d, cfg.fastspeech_num_heads, cfg.fastspeech_filter_size,
                     cfg.fastspeech_kernel_size, cfg.dropout)
            for _ in range(cfg.fastspeech_num_layers)
        ])
        self.mel_p  = nn.Linear(d, n_mels)
        self.pe2    = SinPositionalEncoding(d, drop=cfg.dropout)

    def forward(self, z, z_emo, T, profile=None, f0_gt=None, e_gt=None):
        h    = self.inp(z).unsqueeze(1).expand(-1, max(T // 8, 4), -1)
        h    = self.pe(h)
        for blk in self.ph_enc: h = blk(h)
        eh   = self.emo_p(z_emo).unsqueeze(1).expand_as(h)
        gate = self.gate(torch.cat([h, eh], -1))
        h    = h * gate + eh * (1 - gate)
        dur_pred = F.softplus(self.dur_p(h).squeeze(-1)) + 1.0
        if profile: dur_pred = dur_pred * profile.duration_factor
        h_exp    = self.len_r(h, dur_pred, T)
        pit_pred = self.pit_p(h_exp).squeeze(-1)
        ene_pred = self.ene_p(h_exp).squeeze(-1)
        if profile:
            pit_pred = pit_pred + profile.pitch_shift * 0.1
            pm, ps   = pit_pred.mean(-1, keepdim=True), pit_pred.std(-1, keepdim=True)
            pit_pred = pm + (pit_pred - pm) * (1 + profile.pitch_variance * 2)
            ene_pred = ene_pred * profile.energy_scale
            ene_pred = ene_pred + ene_pred.std(-1, keepdim=True) * profile.energy_variance
        pit_emb = f0_gt if f0_gt is not None else pit_pred
        ene_emb = e_gt  if e_gt  is not None else ene_pred
        h_exp   = h_exp + self.pit_e(pit_emb) + self.ene_e(ene_emb.clamp(0, 150))
        h_exp   = self.pe2(h_exp)
        for blk in self.me_enc: h_exp = blk(h_exp)
        mel = self.mel_p(h_exp).transpose(1, 2)
        if mel.shape[-1] != T:
            mel = F.interpolate(mel, size=T, mode="linear", align_corners=False)
        return {"mel": mel, "pitch_pred": pit_pred, "energy_pred": ene_pred, "dur_pred": dur_pred}


# ─── HiFi-GAN Generator ──────────────────────────────────────────────────────
class HiFiResBlock(nn.Module):
    def __init__(self, ch, k=3, dilations=(1, 3, 5)):
        super().__init__()
        def wn(m): return nn.utils.weight_norm(m)
        pad = lambda k, d: (k * d - d) // 2
        self.c1 = nn.ModuleList([wn(nn.Conv1d(ch, ch, k, dilation=d, padding=pad(k,d))) for d in dilations])
        self.c2 = nn.ModuleList([wn(nn.Conv1d(ch, ch, k, dilation=1, padding=pad(k,1))) for _ in dilations])
    def forward(self, x):
        for a, b in zip(self.c1, self.c2):
            xt = F.leaky_relu(x, 0.1)
            xt = F.leaky_relu(a(xt), 0.1)
            xt = b(xt)
            x  = x + xt
        return x

class HiFiGANGenerator(nn.Module):
    """
    HiFi-GAN V1 generator.

    Input  : mel spectrogram  [B, n_mels, T]  (LINEAR magnitude)
    Output : waveform          [B, 1, T × total_upsample]
             total_upsample = 8 × 8 × 2 × 2 = 256

    Final tanh ensures output ∈ [−1, 1] — eliminates beeping artefacts
    that arise from unbounded activations.
    """
    def __init__(self, cfg: ModelConfig, n_mels: int = 80):
        super().__init__()
        wn = nn.utils.weight_norm
        self.pre  = wn(nn.Conv1d(n_mels, cfg.hifigan_upsample_initial_channel, 7, 1, 3))
        self.ups  = nn.ModuleList()
        self.blks = nn.ModuleList()
        ch = cfg.hifigan_upsample_initial_channel
        for u, k in zip(cfg.hifigan_upsample_rates, cfg.hifigan_upsample_kernel_sizes):
            self.ups.append(wn(nn.ConvTranspose1d(ch, ch // 2, k, u, (k - u) // 2)))
            for rk, rd in zip(cfg.hifigan_resblock_kernel_sizes, cfg.hifigan_resblock_dilation_sizes):
                self.blks.append(HiFiResBlock(ch // 2, rk, rd))
            ch //= 2
        self.post = wn(nn.Conv1d(ch, 1, 7, 1, 3))
        self.nk   = len(cfg.hifigan_resblock_kernel_sizes)
        self.nu   = len(cfg.hifigan_upsample_rates)

    def forward(self, mel):
        x = self.pre(mel)
        for i in range(self.nu):
            x  = F.leaky_relu(x, 0.1)
            x  = self.ups[i](x)
            xs = None
            for j in range(self.nk):
                y  = self.blks[i * self.nk + j](x)
                xs = y if xs is None else xs + y
            x = xs / self.nk
        return torch.tanh(self.post(F.leaky_relu(x, 0.1)))


# ─── Griffin-Lim Fallback Vocoder ────────────────────────────────────────────
class GriffinLimVocoder:
    """
    Correct Griffin-Lim: inverts the mel filterbank via pseudoinverse,
    then runs iterative phase recovery (80 iterations).

    Input  : mel_linear  np.ndarray [n_mels, T]  (LINEAR magnitude, not dB)
    Output : waveform    torch.Tensor [1, samples]

    Critical: input MUST be linear magnitude, not dB-scaled.
    Passing dB values produces extremely loud/clipped output.
    """
    def __init__(self, cfg: AudioConfig, n_iter: int = 80):
        self.cfg    = cfg
        self.n_iter = n_iter
        fb = librosa.filters.mel(
            sr=cfg.sample_rate, n_fft=cfg.n_fft, n_mels=cfg.n_mels,
            fmin=cfg.fmin, fmax=cfg.fmax,
        )
        self.fb_inv = np.linalg.pinv(fb)

    def __call__(self, mel_linear: np.ndarray) -> torch.Tensor:
        stft_mag = np.maximum(0, self.fb_inv @ mel_linear)
        ang = np.exp(1j * np.random.uniform(0, 2 * np.pi, stft_mag.shape))
        for i in range(self.n_iter):
            wav = librosa.istft(stft_mag * ang, hop_length=self.cfg.hop_length,
                                win_length=self.cfg.win_length, n_fft=self.cfg.n_fft)
            if i < self.n_iter - 1:
                stft = librosa.stft(wav, n_fft=self.cfg.n_fft,
                                    hop_length=self.cfg.hop_length,
                                    win_length=self.cfg.win_length)
                ang  = np.exp(1j * np.angle(stft))
        return torch.from_numpy(wav.astype(np.float32)).unsqueeze(0)


# ─── NaturalPostProcessor ────────────────────────────────────────────────────
class NaturalPostProcessor:
    """
    7-stage post-processor that removes the 'robot' quality from synthesised speech:

    Stage 1: Spectral shaping   — formant emphasis / breathiness / pressed phonation
    Stage 2: Jitter & shimmer   — micro pitch/amplitude perturbation (~1-8 ms cycles)
    Stage 3: Breath modulation  — amplitude envelope following natural breathing rate
    Stage 4: Air noise          — low-level band-limited noise for realism
    Stage 5: DRC compression    — soft knee at 0.32 with ratio 4:1
    Stage 6: De-click smoothing — hanning kernel on transient peaks > 0.15
    Stage 7: Soft limiter       — tanh @ 1.05x to prevent clipping

    All stages operate on float64 for numerical stability; output cast to float32.
    """
    def __init__(self, sr: int = 22050):
        self.sr = sr

    def process(self, wav: torch.Tensor, profile: EmotionProfile) -> torch.Tensor:
        w = wav.squeeze().numpy().astype(np.float64)
        w = self._spectral_shape(w, profile)
        w = self._jitter_shimmer(w, profile)
        w = self._breath_mod(w, profile)
        w = self._air_noise(w, profile)
        w = self._compress(w)
        w = self._declick(w)
        w = self._soft_limit(w)
        w = w / (np.abs(w).max() + 1e-8) * 0.88
        return torch.from_numpy(w.astype(np.float32)).unsqueeze(0)

    def _spectral_shape(self, w, p):
        sr = self.sr
        if p.voice_quality == "bright":
            b, a = signal.butter(2, 2800 / (sr / 2), "high")
            w    = w + 0.18 * signal.filtfilt(b, a, w)
        elif p.voice_quality == "breathy":
            b, a = signal.butter(2, 3500 / (sr / 2), "low")
            w    = signal.filtfilt(b, a, w)
            bn, an = signal.butter(2, [1500 / (sr / 2), 6000 / (sr / 2)], "band")
            w    = w + signal.filtfilt(bn, an, np.random.randn(len(w))) * 0.012
        elif p.voice_quality == "tense":
            b, a = signal.butter(2, [1200 / (sr / 2), 4500 / (sr / 2)], "band")
            w    = w + 0.22 * signal.filtfilt(b, a, w)
            w    = np.tanh(w * 1.35) / np.tanh(np.float64(1.35))
        elif p.voice_quality == "tremulous":
            t    = np.arange(len(w)) / sr
            freq = np.random.uniform(4.8, 6.2)
            w    = w * (1.0 + 0.035 * np.sin(2 * np.pi * freq * t))
        elif p.voice_quality == "creaky":
            t    = np.arange(len(w)) / sr
            creak = 0.04 * np.sin(2 * np.pi * 80 * t) * (1 + 0.5 * np.sin(2 * np.pi * 2 * t))
            b, a  = signal.butter(2, 200 / (sr / 2), "low")
            w    += signal.filtfilt(b, a, creak * np.abs(w))
        return w

    def _jitter_shimmer(self, w, p):
        jit  = {"modal":0.002,"bright":0.003,"breathy":0.006,"tense":0.004,
                "tremulous":0.008,"creaky":0.003}.get(p.voice_quality, 0.003)
        shim = {"modal":0.03,"bright":0.04,"breathy":0.05,"tense":0.035,
                "tremulous":0.07,"creaky":0.04}.get(p.voice_quality, 0.03)
        rng  = np.random.default_rng(seed=42)
        j    = np.convolve(1 + rng.normal(0, jit,  len(w)), np.ones(60)  / 60,  "same")
        s    = np.convolve(1 + rng.normal(0, shim, len(w)), np.ones(120) / 120, "same")
        return w * j * s

    def _breath_mod(self, w, p):
        t  = np.arange(len(w)) / self.sr
        br = 0.28 if p.speed_factor < 0.9 else 0.42
        return w * (1 + 0.018 * np.sin(2*np.pi*br*t)) * (1 + 0.032 * np.sin(2*np.pi*0.07*t + np.pi/3))

    def _air_noise(self, w, p):
        lvl  = {"breathy":0.009,"modal":0.003,"bright":0.004,
                "tense":0.002,"tremulous":0.005,"creaky":0.003}.get(p.voice_quality, 0.003)
        noise = np.random.randn(len(w)) * lvl
        b, a  = signal.butter(1, 4000 / (self.sr / 2), "low")
        return w + signal.lfilter(b, a, noise)

    def _compress(self, w, thr=0.32, ratio=4.0):
        c    = w.copy()
        mask = np.abs(w) > thr
        c[mask] = np.sign(w[mask]) * (thr + (np.abs(w[mask]) - thr) / ratio)
        return c

    def _declick(self, w, win=64):
        kernel = np.hanning(win) / np.hanning(win).sum()
        pad    = np.pad(w, win // 2, mode="edge")
        smooth = np.convolve(pad, kernel, "same")[win // 2: win // 2 + len(w)]
        diff   = np.abs(np.diff(np.abs(w), prepend=0))
        w[diff > 0.15] = smooth[diff > 0.15]
        return w

    def _soft_limit(self, w):
        return np.tanh(w * 1.05) / np.tanh(np.float64(1.05))


# ─── EmotionalSpeechConverter ────────────────────────────────────────────────
class EmotionalSpeechConverter(nn.Module):
    """
    End-to-end pipeline:
      WAV → AudioProcessor → mel_db
      mel_db → VAE → z
      (z, src_emo, tgt_emo) → GNN → z_target
      (z, z_target) → FastSpeech2 → mel_predicted
      mel_predicted → HiFi-GAN → waveform
      waveform → NaturalPostProcessor → clean, human-sounding audio
    """
    def __init__(self, model_cfg: ModelConfig, audio_cfg: AudioConfig):
        super().__init__()
        n = audio_cfg.n_mels
        self.mcfg    = model_cfg
        self.acfg    = audio_cfg
        self.vae     = VoiceVAE(model_cfg, n)
        self.gnn     = EmotionGNN(model_cfg)
        self.fs2     = FastSpeech2VarianceAdaptor(model_cfg, n)
        self.vocoder = HiFiGANGenerator(model_cfg, n)
        self.gl_voc  = GriffinLimVocoder(audio_cfg, n_iter=80)
        self.audio   = AudioProcessor(audio_cfg)
        self.post    = NaturalPostProcessor(audio_cfg.sample_rate)

    def forward(self, mel_db, src_id, tgt_id, prosody=None, T=None, profile=None):
        T = T or mel_db.shape[-1]
        vae_out = self.vae(mel_db, src_id)
        gnn_out = self.gnn(vae_out["z"], src_id, tgt_id, prosody)
        fs2_out = self.fs2(vae_out["z"], gnn_out["z_target"], T, profile)
        wav     = self.vocoder(fs2_out["mel"])
        return {**vae_out, **gnn_out, **fs2_out, "waveform": wav,
                "mel_converted": fs2_out["mel"]}

    @torch.no_grad()
    def convert(self, input_wav: torch.Tensor, src_emo: str,
                tgt_emo: str) -> Tuple[torch.Tensor, Dict]:
        self.eval()
        device = next(self.parameters()).device
        feats  = self.audio.extract_features(input_wav)
        mel_db = feats["mel_db"].unsqueeze(0).to(device)
        T      = mel_db.shape[-1]
        sid    = torch.tensor([EMOTION_IDX[src_emo]], device=device)
        tid    = torch.tensor([EMOTION_IDX[tgt_emo]], device=device)
        pro    = EMOTION_PROFILES[tgt_emo]

        f0_v   = feats["f0"][feats["voiced"] > 0.5]
        prosody = torch.tensor([[
            f0_v.mean().item() if len(f0_v) else 0.0,
            f0_v.std().item()  if len(f0_v) > 1 else 0.0,
            feats["energy"].mean().item(),
            feats["energy"].std().item(),
            pro.speed_factor,
            pro.duration_factor,
        ]], device=device)

        out     = self.forward(mel_db, sid, tid, prosody, T, pro)
        wav     = input_wav.to(device)
        wav     = self.audio.pitch_shift(wav, pro.pitch_shift)
        wav     = self.audio.time_stretch(wav, pro.speed_factor)
        mel_lin = self.audio.wav_to_mel_linear(wav)
        wav_gl  = self.gl_voc(mel_lin * pro.energy_scale)
        return self.post.process(wav_gl, pro), out

    def count_params(self):
        d = {n: sum(p.numel() for p in m.parameters() if p.requires_grad)
             for n, m in [("vae", self.vae), ("gnn", self.gnn),
                           ("fs2", self.fs2), ("vocoder", self.vocoder)]}
        d["total"] = sum(d.values())
        return d


