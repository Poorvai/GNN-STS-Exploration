# ║  CELL 7 ★ NEW — FRAME-LEVEL EMOTION TRAJECTORY                         ║
# ║                                                                          ║
# ║  What it does:                                                           ║
# ║    Splits the converted audio into overlapping frames and runs a         ║
# ║    sliding-window MFCC classifier to estimate emotion probability at     ║
# ║    every frame.  This shows how emotional content evolves through the    ║
# ║    sentence — e.g. a "happy" utterance may start neutral, rise sharply   ║
# ║    at the stressed syllable, and trail off.                              ║
# ║                                                                          ║
# ║    Output includes:                                                      ║
# ║      • Temporal heatmap of all 7 emotion probabilities                  ║
# ║      • F0 pitch contour overlaid on the dominant emotion                 ║
# ║      • Energy envelope overlaid on the emotion trajectory                ║
# ║                                                                          ║
# ║  Why it matters:                                                         ║
# ║    A static emotion label hides temporal dynamics. Frame-level           ║
# ║    analysis reveals phrasing, emphasis and prosodic contours that        ║
# ║    make emotional speech perceptually rich.                              ║
# ╚══════════════════════════════════════════════════════════════════════════╝

def compute_frame_emotion_trajectory(
    wav: torch.Tensor,
    sr:  int,
    frame_size_ms: float = 40.0,
    hop_ms:        float = 10.0,
    n_mfcc:        int   = 40,
) -> Dict[str, np.ndarray]:
    """
    Sliding-window MFCC-based frame-level emotion probability estimator.

    Uses a simple cosine-similarity classifier against per-emotion MFCC
    prototype vectors (derived from emotion profile parameters) to produce
    a pseudo-probability at every frame.  No external model needed.

    Returns
    ───────
    {
        "times":        [T_frames]       frame centre times in seconds
        "probs":        [T_frames, 7]    emotion probabilities per frame
        "f0":           [T_frames]       estimated F0 per frame (Hz, 0=unvoiced)
        "energy":       [T_frames]       RMS energy per frame
        "dominant":     [T_frames]       argmax emotion index per frame
    }
    """
    w          = wav.squeeze().cpu().numpy().astype(np.float32)
    frame_size = int(sr * frame_size_ms / 1000)
    hop        = int(sr * hop_ms / 1000)
    n_frames   = (len(w) - frame_size) // hop + 1

    # ── Build prototype MFCC fingerprints from profile params ─────────────
    prototypes = {}
    for emo, p in EMOTION_PROFILES.items():
        t_proto = np.linspace(0, 0.5, int(sr * 0.5))
        f0_hz   = 120 * (2 ** (p.pitch_shift / 12))
        phase   = np.cumsum(2 * np.pi * f0_hz / sr)
        synth   = sum(np.sin(k * phase * (1 + 0.01 * p.pitch_variance)) / k
                      for k in range(1, 8))
        synth  *= p.energy_scale
        synth  /= (np.abs(synth).max() + 1e-8)
        mfcc_p  = librosa.feature.mfcc(y=synth.astype(np.float32), sr=sr, n_mfcc=n_mfcc)
        prototypes[emo] = mfcc_p.mean(1)

    # ── Frame-level analysis ───────────────────────────────────────────────
    times  = np.array([(i * hop + frame_size // 2) / sr for i in range(n_frames)])
    probs  = np.zeros((n_frames, 7), dtype=np.float32)
    f0_arr = np.zeros(n_frames, dtype=np.float32)
    e_arr  = np.zeros(n_frames, dtype=np.float32)

    for i in range(n_frames):
        frame = w[i * hop: i * hop + frame_size]
        if len(frame) < frame_size: break

        mfcc_f = librosa.feature.mfcc(y=frame, sr=sr, n_mfcc=n_mfcc).mean(1)
        e_arr[i] = float(np.sqrt(np.mean(frame ** 2)))

        # F0 via autocorrelation
        corr = np.correlate(frame, frame, mode="full")
        corr = corr[len(corr) // 2:]
        min_lag = int(sr / 400); max_lag = int(sr / 80)
        if max_lag < len(corr):
            peak = np.argmax(corr[min_lag:max_lag]) + min_lag
            if corr[peak] / (corr[0] + 1e-8) > 0.4:
                f0_arr[i] = sr / peak

        # Cosine similarity to each prototype
        sims = np.array([
            float(np.dot(mfcc_f, prototypes[e]) /
                  (np.linalg.norm(mfcc_f) * np.linalg.norm(prototypes[e]) + 1e-8))
            for e in EMO_LIST
        ])
        sims = np.clip(sims + 1, 0, None)
        probs[i] = sims / (sims.sum() + 1e-8)

    dominant = probs.argmax(axis=1)
    return {"times": times, "probs": probs, "f0": f0_arr, "energy": e_arr, "dominant": dominant}


