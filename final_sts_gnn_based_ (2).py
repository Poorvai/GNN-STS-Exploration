# GRAPH 2: Per-layer GAT attention heatmaps
# ─────────────────────────────────────────────────────────────────────────────
def plot_gat_attention(source_emo="neutral", target_emo="happy"):
    """Run one GNN forward pass, capture attention weights, plot heatmaps."""
    model.eval()
    gnn_module = model.gnn

    # Forward pass to populate attention caches
    device = next(model.parameters()).device
    z_dummy = torch.randn(1, MODEL_CFG.latent_dim, device=device)
    sid = torch.tensor([EMOTION_IDX[source_emo]], device=device)
    tid = torch.tensor([EMOTION_IDX[target_emo]], device=device)
    with torch.no_grad():
        _ = gnn_module(z_dummy, sid, tid)

    attn_per_layer = gnn_module.last_attention_per_layer
    if not attn_per_layer:
        print("No attention weights cached — run conversion first."); return

    batch_0 = attn_per_layer[0]   # List[Tensor] one per layer
    n_layers = len(batch_0)
    ei = gnn_module.edge_index.cpu().numpy()   # [2, E]

    n_emo = len(EMO_LIST)
    fig, axes = plt.subplots(2, max(n_layers//2, 2), figsize=(22, 10))
    axes = axes.flatten()
    fig.suptitle(f"GAT ATTENTION WEIGHTS PER LAYER  |  {source_emo.upper()} → {target_emo.upper()}",
                 fontsize=14, fontweight="bold", color="#58a6ff")

    cmap_attn = LinearSegmentedColormap.from_list("attn", ["#0d1117","#1f4f7a","#2563eb","#60a5fa","#fbbf24","#f97316"])

    for li in range(n_layers):
        ax = axes[li]
        attn_vals = batch_0[li].cpu().numpy()   # shape [E]

        # Build N×N attention matrix
        A = np.zeros((n_emo, n_emo))
        for k, (s, t) in enumerate(zip(ei[0], ei[1])):
            A[t, s] = attn_vals[k] if k < len(attn_vals) else 0.0

        im = ax.imshow(A, cmap=cmap_attn, aspect='auto', vmin=0)
        ax.set_xticks(range(n_emo)); ax.set_xticklabels([e[:3].upper() for e in EMO_LIST], fontsize=8)
        ax.set_yticks(range(n_emo)); ax.set_yticklabels([e[:5].upper() for e in EMO_LIST], fontsize=8)
        ax.set_title(f"Layer {li+1}", color="#79c0ff", fontsize=10)
        ax.set_xlabel("Source node", fontsize=7); ax.set_ylabel("Target node", fontsize=7)

        # Highlight source → target
        src_i = EMOTION_IDX[source_emo]; tgt_i = EMOTION_IDX[target_emo]
        rect = plt.Rectangle((src_i-0.5, tgt_i-0.5), 1, 1, fill=False, edgecolor="#f78166", lw=2.5)
        ax.add_patch(rect)

        # Annotate cells
        for r in range(n_emo):
            for c in range(n_emo):
                v = A[r, c]
                if v > 0.01:
                    ax.text(c, r, f"{v:.2f}", ha='center', va='center',
                            fontsize=6, color='white' if v > 0.3 else '#8b949e')

        fig.colorbar(im, ax=ax, shrink=0.8)

    # Hide unused axes
    for ax in axes[n_layers:]: ax.set_visible(False)

    plt.tight_layout(rect=[0, 0, 1, 0.95])
    _show(fig)
    print(f"Graph 2: GAT attention heatmaps for {n_layers} layers rendered.")


