class NaturalPostProcessor:
    """
    Layered post-processing pipeline designed to eliminate the 'robot' quality:

    1. Spectral shaping per voice quality (formant emphasis, breathiness, etc.)
    2. Micro jitter + shimmer (modelled on human vocal fold irregularity)
    3. Sub-band noise colouring (air, presence, room)
    4. Amplitude breath modulation (natural breathing rhythm)
    5. Dynamic range compression (like a well-set vocal compressor)
    6. De-click / anti-transient smoothing
    7. Final soft limiter + normalisation
    """
    def __init__(self, sr=22050):
        self.sr = sr

    def process(self, wav: torch.Tensor, profile: EmotionProfile) -> torch.Tensor:
        w = wav.squeeze().numpy().astype(np.float64)
        w = self._spectral_shape(w, profile)
        w = self._jitter_shimmer(w, profile)
        w = self._breath_mod(w, profile)
        w = self._air_noise(w, profile)
        w = self._compress(w)
        w = self._declick(w)
        w = self._soft_limit(w)
        w = w / (np.abs(w).max() + 1e-8) * 0.88
        return torch.from_numpy(w.astype(np.float32)).unsqueeze(0)

    def _spectral_shape(self, w, p):
        sr = self.sr
        if p.voice_quality == "bright":
            b, a = signal.butter(2, 2800/(sr/2), 'high')
            w = w + 0.18 * signal.filtfilt(b, a, w)
        elif p.voice_quality == "breathy":
            # Reduce spectral tilt, add band-limited breath noise
            b, a = signal.butter(2, 3500/(sr/2), 'low')
            w = signal.filtfilt(b, a, w)
            bn, an = signal.butter(2, [1500/(sr/2), 6000/(sr/2)], 'band')
            w = w + signal.filtfilt(bn, an, np.random.randn(len(w))) * 0.012
        elif p.voice_quality == "tense":
            b, a = signal.butter(2, [1200/(sr/2), 4500/(sr/2)], 'band')
            w = w + 0.22 * signal.filtfilt(b, a, w)
            # Soft clipping for pressed phonation
            w = np.tanh(w * 1.35) / np.tanh(np.float64(1.35))
        elif p.voice_quality == "tremulous":
            t = np.arange(len(w)) / sr
            freq = np.random.uniform(4.8, 6.2)
            w = w * (1.0 + 0.035 * np.sin(2*np.pi*freq*t))
        elif p.voice_quality == "creaky":
            # Sub-harmonic at ~80 Hz
            t = np.arange(len(w)) / sr
            creak = 0.04 * np.sin(2*np.pi*80*t) * (1 + 0.5*np.sin(2*np.pi*2*t))
            b, a = signal.butter(2, 200/(sr/2), 'low')
            w = w + signal.filtfilt(b, a, creak * np.abs(w))
        return w

    def _jitter_shimmer(self, w, p):
        """
        Jitter = cycle-to-cycle pitch frequency perturbation
        Shimmer = cycle-to-cycle amplitude perturbation
        Both make the voice sound organic, not synthesised.
        """
        jit = {"modal":0.002,"bright":0.003,"breathy":0.006,"tense":0.004,
               "tremulous":0.008,"creaky":0.003}.get(p.voice_quality, 0.003)
        shim = {"modal":0.03,"bright":0.04,"breathy":0.05,"tense":0.035,
                "tremulous":0.07,"creaky":0.04}.get(p.voice_quality, 0.03)
        rng = np.random.default_rng(seed=42)
        j = np.convolve(1 + rng.normal(0, jit, len(w)), np.ones(60)/60, 'same')
        s = np.convolve(1 + rng.normal(0, shim, len(w)), np.ones(120)/120, 'same')
        return w * j * s

    def _breath_mod(self, w, p):
        t = np.arange(len(w)) / self.sr
        br = 0.28 if p.speed_factor < 0.9 else 0.42
        env = (1 + 0.018 * np.sin(2*np.pi*br*t)) * (1 + 0.032 * np.sin(2*np.pi*0.07*t + np.pi/3))
        return w * env

    def _air_noise(self, w, p):
        level = {"breathy":0.009,"modal":0.003,"bright":0.004,"tense":0.002,
                 "tremulous":0.005,"creaky":0.003}.get(p.voice_quality, 0.003)
        noise = np.random.randn(len(w)) * level
        b, a = signal.butter(1, 4000/(self.sr/2), 'low')
        return w + signal.lfilter(b, a, noise)

    def _compress(self, w, thr=0.32, ratio=4.0):
        c = w.copy()
        mask = np.abs(w) > thr
        c[mask] = np.sign(w[mask]) * (thr + (np.abs(w[mask]) - thr) / ratio)
        return c

    def _declick(self, w, win=64):
        """Smooth abrupt transitions that cause clicks"""
        kernel = np.hanning(win) / np.hanning(win).sum()
        pad = np.pad(w, win//2, mode='edge')
        smooth = np.convolve(pad, kernel, 'same')[win//2: win//2+len(w)]
        diff = np.abs(np.diff(np.abs(w), prepend=0))
        click_mask = diff > 0.15
        w[click_mask] = smooth[click_mask]
        return w

    def _soft_limit(self, w):
        return np.tanh(w * 1.05) / np.tanh(np.float64(1.05))


