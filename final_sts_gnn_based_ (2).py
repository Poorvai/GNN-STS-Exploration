class SinPositionalEncoding(nn.Module):
    def __init__(self, d, maxlen=5000, drop=0.1):
        super().__init__()
        self.drop = nn.Dropout(drop)
        pe = torch.zeros(maxlen, d)
        pos = torch.arange(0, maxlen).float().unsqueeze(1)
        div = torch.exp(torch.arange(0, d, 2).float() * (-math.log(10000.0)/d))
        pe[:, 0::2] = torch.sin(pos * div)
        pe[:, 1::2] = torch.cos(pos * div)
        self.register_buffer("pe", pe.unsqueeze(0))
    def forward(self, x): return self.drop(x + self.pe[:, :x.size(1)])

class FFTBlock(nn.Module):
    """Feed-Forward Transformer block — backbone of FastSpeech encoder/decoder"""
    def __init__(self, d, heads, d_ff, k=9, drop=0.1):
        super().__init__()
        self.attn = nn.MultiheadAttention(d, heads, dropout=drop, batch_first=True)
        self.n1   = nn.LayerNorm(d); self.n2 = nn.LayerNorm(d)
        p = (k-1)//2
        self.ffn  = nn.Sequential(
            nn.Conv1d(d, d_ff, k, padding=p), nn.GELU(), nn.Dropout(drop),
            nn.Conv1d(d_ff, d, k, padding=p)
        )
        self.drop = nn.Dropout(drop)
    def forward(self, x, mask=None):
        a, _ = self.attn(x, x, x, key_padding_mask=mask)
        x = self.n1(x + self.drop(a))
        f = self.ffn(x.transpose(1,2)).transpose(1,2)
        return self.n2(x + self.drop(f))

class VariancePredictor(nn.Module):
    def __init__(self, d, out=1, k=3):
        super().__init__()
        p = (k-1)//2
        self.net = nn.Sequential(
            nn.Conv1d(d, d, k, padding=p), nn.GELU(), nn.LayerNorm(d),
            nn.Conv1d(d, d, k, padding=p), nn.GELU(), nn.LayerNorm(d),
            nn.Linear(d, out)
        )
    def forward(self, x):
        h = x
        for layer in self.net:
            if isinstance(layer, nn.Conv1d):     h = layer(h.transpose(1,2)).transpose(1,2)
            elif isinstance(layer, nn.LayerNorm): h = layer(h)
            else:                                 h = layer(h)
        return h

class PitchEmbedding(nn.Module):
    def __init__(self, bins, d, f0min=-3.0, f0max=3.0):
        super().__init__()
        self.emb = nn.Embedding(bins+1, d)
        self.register_buffer("b", torch.linspace(f0min, f0max, bins))
        self.bins = bins
    def forward(self, f0):
        idx = torch.bucketize(f0.clamp(self.b[0], self.b[-1]), self.b).clamp(0, self.bins)
        return self.emb(idx)

class EnergyEmbedding(nn.Module):
    def __init__(self, bins, d, emin=0.0, emax=150.0):
        super().__init__()
        self.emb = nn.Embedding(bins+1, d)
        self.register_buffer("b", torch.linspace(emin, emax, bins))
        self.bins = bins
    def forward(self, e):
        idx = torch.bucketize(e.clamp(self.b[0], self.b[-1]), self.b).clamp(0, self.bins)
        return self.emb(idx)

class LengthRegulator(nn.Module):
    """Expands phoneme sequence to frame sequence using predicted durations"""
    def forward(self, x, dur, T):
        out = []
        for b in range(x.shape[0]):
            d = dur[b].clamp(min=1).long()
            d = (d.float() * T / d.sum().clamp(1)).round().clamp(1).long()
            rep = torch.repeat_interleave(x[b], d, dim=0)
            if rep.shape[0] >= T: rep = rep[:T]
            else: rep = F.pad(rep, (0,0,0,T-rep.shape[0]))
            out.append(rep)
        return torch.stack(out, 0)

class FastSpeech2VarianceAdaptor(nn.Module):
    """
    FastSpeech 2 controls WHAT the mel looks like given the GNN's WHAT emotion.
    The emotion gating here blends content hidden states with emotion-specific
    representations so every prosodic dimension (pitch, energy, duration) is
    conditioned on the target emotion simultaneously.
    """
    def __init__(self, cfg: ModelConfig, n_mels=80):
        super().__init__()
        d = cfg.fastspeech_hidden
        self.inp    = nn.Linear(cfg.latent_dim, d)
        self.pe     = SinPositionalEncoding(d, drop=cfg.dropout)
        self.ph_enc = nn.ModuleList([FFTBlock(d, cfg.fastspeech_num_heads, cfg.fastspeech_filter_size, cfg.fastspeech_kernel_size, cfg.dropout) for _ in range(cfg.fastspeech_num_layers)])
        self.dur_p  = VariancePredictor(d)
        self.len_r  = LengthRegulator()
        self.pit_p  = VariancePredictor(d)
        self.ene_p  = VariancePredictor(d)
        self.pit_e  = PitchEmbedding(cfg.pitch_bins, d)
        self.ene_e  = EnergyEmbedding(cfg.energy_bins, d)
        self.emo_p  = nn.Linear(cfg.latent_dim, d)
        self.gate   = nn.Sequential(nn.Linear(d*2, d), nn.Sigmoid())
        self.me_enc = nn.ModuleList([FFTBlock(d, cfg.fastspeech_num_heads, cfg.fastspeech_filter_size, cfg.fastspeech_kernel_size, cfg.dropout) for _ in range(cfg.fastspeech_num_layers)])
        self.mel_p  = nn.Linear(d, n_mels)
        self.pe2    = SinPositionalEncoding(d, drop=cfg.dropout)

    def forward(self, z, z_emo, T, profile=None, f0_gt=None, e_gt=None):
        B = z.shape[0]
        h = self.inp(z).unsqueeze(1).expand(-1, max(T//8,4), -1)
        h = self.pe(h)
        for blk in self.ph_enc: h = blk(h)

        # Emotion gating: soft blend between content and emotion representations
        eh   = self.emo_p(z_emo).unsqueeze(1).expand_as(h)
        gate = self.gate(torch.cat([h, eh], -1))
        h    = h * gate + eh * (1 - gate)

        dur_pred = F.softplus(self.dur_p(h).squeeze(-1)) + 1.0
        if profile: dur_pred = dur_pred * profile.duration_factor
        h_exp = self.len_r(h, dur_pred, T)

        pit_pred = self.pit_p(h_exp).squeeze(-1)
        ene_pred = self.ene_p(h_exp).squeeze(-1)

        # Apply emotion profile shifts in prediction space
        if profile:
            pit_pred = pit_pred + profile.pitch_shift * 0.1
            pm, ps = pit_pred.mean(-1, keepdim=True), pit_pred.std(-1, keepdim=True)
            pit_pred = pm + (pit_pred - pm) * (1 + profile.pitch_variance * 2)
            ene_pred = ene_pred * profile.energy_scale
            ene_pred = ene_pred + ene_pred.std(-1, keepdim=True) * profile.energy_variance

        pit_for_emb = f0_gt if f0_gt is not None else pit_pred
        ene_for_emb = e_gt  if e_gt  is not None else ene_pred

        h_exp = h_exp + self.pit_e(pit_for_emb) + self.ene_e(ene_for_emb.clamp(0, 150))
        h_exp = self.pe2(h_exp)
        for blk in self.me_enc: h_exp = blk(h_exp)
        mel = self.mel_p(h_exp).transpose(1, 2)
        if mel.shape[-1] != T:
            mel = F.interpolate(mel, size=T, mode='linear', align_corners=False)
        return {"mel": mel, "pitch_pred": pit_pred, "energy_pred": ene_pred, "dur_pred": dur_pred}


