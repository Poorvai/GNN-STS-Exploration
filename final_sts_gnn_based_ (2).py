# ─────────────────────────────────────────────────────────────────────────────
# GRAPH 4: Message-passing flow visualisation
# ─────────────────────────────────────────────────────────────────────────────
def plot_message_passing_flow(source_emo="neutral", target_emo="happy"):
    """
    Shows how information flows through the emotion graph across L layers.
    Each column = one GAT layer. Node size ∝ activation norm at that layer.
    Edge thickness ∝ attention weight. Highlighted path = source → target.
    """
    fig, axes = plt.subplots(1, MODEL_CFG.gnn_num_layers, figsize=(24, 8))
    fig.suptitle(
        f"MESSAGE PASSING FLOW  ({MODEL_CFG.gnn_num_layers} GAT layers)  "
        f"|  {source_emo.upper()} → {target_emo.upper()}",
        fontsize=14, fontweight="bold", color="#58a6ff"
    )

    model.eval()
    gnn_module = model.gnn
    device = next(model.parameters()).device
    ei = gnn_module.edge_index.cpu().numpy()

    z_dummy = torch.randn(1, MODEL_CFG.latent_dim, device=device)
    sid = torch.tensor([EMOTION_IDX[source_emo]], device=device)
    tid = torch.tensor([EMOTION_IDX[target_emo]], device=device)
    with torch.no_grad():
        _ = gnn_module(z_dummy, sid, tid)

    attn_layers = gnn_module.last_attention_per_layer
    if not attn_layers: print("No attention cached."); return
    batch_0 = attn_layers[0]

    pos = {i: VA_POS[n] for i, n in enumerate(EMO_LIST)}

    for li, ax in enumerate(axes):
        if li >= len(batch_0): ax.set_visible(False); continue
        ax.set_facecolor("#0d1117")
        attn_arr = batch_0[li].cpu().numpy()   # [E]

        # Build edge dict
        edge_attn = {}
        for k, (s, t) in enumerate(zip(ei[0], ei[1])):
            edge_attn[(s, t)] = attn_arr[k] if k < len(attn_arr) else 0.0

        # Compute node activations this layer (re-run partially — use norms approx.)
        node_norms = np.zeros(len(EMO_LIST))
        for s, t in zip(ei[0], ei[1]):
            node_norms[t] += edge_attn.get((s, t), 0.0)
        node_norms = (node_norms - node_norms.min()) / (node_norms.max() - node_norms.min() + 1e-8)

        # Draw edges
        src_i = EMOTION_IDX[source_emo]; tgt_i = EMOTION_IDX[target_emo]
        for (s, t), w in edge_attn.items():
            if w < 0.01: continue
            xs, ys = pos[s]; xt, yt = pos[t]
            on_path = (s == src_i and t == tgt_i) or (s == tgt_i)
            color = "#f78166" if on_path else f"#{int(100+w*155):02x}{int(50+w*100):02x}ff"
            lw = 0.5 + w * 5.0 if not on_path else 0.5 + w * 7.0
            ax.annotate("", xy=(xt, yt), xytext=(xs, ys),
                        arrowprops=dict(arrowstyle="-|>", color=color,
                                       lw=lw, alpha=min(1.0, 0.3 + w*1.5),
                                       mutation_scale=10 + w*8))

        # Draw nodes
        for i, emo in enumerate(EMO_LIST):
            vx, vy = pos[i]
            base_size = 200 + node_norms[i] * 600
            ring = "#ffffff" if i in (src_i, tgt_i) else "#30363d"
            ring_lw = 3.0 if i in (src_i, tgt_i) else 1.0
            ax.scatter(vx, vy, s=base_size, c=COLORS[emo],
                       edgecolors=ring, linewidths=ring_lw, zorder=5)
            off = 0.065 if vy < 0.85 else -0.07
            ax.text(vx, vy + off, emo[:3].upper(),
                    ha='center', va='center', fontsize=8.5,
                    color=COLORS[emo], fontweight='bold',
                    path_effects=[pe.withStroke(linewidth=3, foreground='#0d1117')],
                    zorder=6)

        mean_attn = np.mean(list(edge_attn.values()))
        ax.set_title(f"Layer {li+1}\nMean Attn={mean_attn:.3f}", color="#79c0ff", fontsize=9)
        ax.set_xlim(-0.08, 1.08); ax.set_ylim(-0.12, 1.15)
        ax.set_xlabel("Valence", fontsize=7); ax.set_ylabel("Arousal" if li == 0 else "", fontsize=7)
        ax.grid(True, alpha=0.2)

    plt.tight_layout(rect=[0, 0, 1, 0.93])
    _show(fig)
    print(f"Graph 4: Message-passing flow across {MODEL_CFG.gnn_num_layers} layers rendered.")


