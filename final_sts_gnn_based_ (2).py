    sid = torch.tensor([EMOTION_IDX[source_emo]], device=device)
    tid = torch.tensor([EMOTION_IDX[EMO_LIST[-1]]], device=device)
    with torch.no_grad(): gnn_module(z_dummy, sid, tid)
    if gnn_module.last_node_activations is not None:
        act_mat = gnn_module.last_node_activations[:, :64].numpy()  # [7, 64]
        im = ax_c.imshow(act_mat, cmap="RdYlBu_r", aspect='auto')
        ax_c.set_yticks(range(len(EMO_LIST)))
        ax_c.set_yticklabels([e.upper() for e in EMO_LIST], fontsize=9)
        ax_c.set_xlabel("Hidden Dimension (first 64)", fontsize=9)
        ax_c.set_title("Node Hidden Activation Map (after all GAT layers)", color="#79c0ff", fontsize=10)
        fig.colorbar(im, ax=ax_c, shrink=0.6)

    # ── Panel D: prosody radar charts per emotion ───────────────────────────
    categories = ["Pitch Shift", "Speed", "Energy", "Pitch Var.", "Energy Var.", "Duration"]
    n_cat = len(categories)
    angles = np.linspace(0, 2*np.pi, n_cat, endpoint=False).tolist() + [0]

    for idx, emo in enumerate(EMO_LIST):
        ax_r = fig.add_subplot(gs[2, idx % 3] if idx < 3 else gs[2, idx % 3],
                               projection='polar')
        # Only add 4 radar charts to fit the layout
        if idx >= 4: break
        p = EMOTION_PROFILES[emo]

        def norm(v, mn, mx): return (v - mn)/(mx - mn + 1e-8)
