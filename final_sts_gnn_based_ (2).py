def plot_va_graph(source_emo="neutral", target_emo="happy"):
    fig, axes = plt.subplots(1, 2, figsize=(20, 9))
    fig.suptitle("EMOTION GRAPH — Valence × Arousal Space", fontsize=16,
                 fontweight="bold", color="#58a6ff", y=0.98)

    # Build NetworkX graph
    G = nx.DiGraph()
    for emo in EMO_LIST:
        G.add_node(emo, valence=VA_POS[emo][0], arousal=VA_POS[emo][1])

    for e1 in EMO_LIST:
        for e2 in EMO_LIST:
            if e1 != e2:
                v1, a1 = VA_POS[e1]; v2, a2 = VA_POS[e2]
                sim = 1.0 - np.sqrt((v1-v2)**2 + (a1-a2)**2) / np.sqrt(2)
                G.add_edge(e1, e2, weight=sim)

    pos = {n: VA_POS[n] for n in EMO_LIST}

    for ax_idx, ax in enumerate(axes):
        ax.set_facecolor("#0d1117")
        ax.set_title(["All Edges (Coloured by Weight)", f"Active Path: {source_emo.upper()} → {target_emo.upper()}"][ax_idx],
                     color="#58a6ff", fontsize=12, pad=10)

        # Background grid (VA quadrant labels)
        for x, lbl in [(0.25, "Negative\nValence"), (0.75, "Positive\nValence")]:
            ax.text(x, -0.06, lbl, ha='center', va='center', fontsize=8,
                    color="#444c56", transform=ax.transAxes)
        for y, lbl in [(0.1, "Low\nArousal"), (0.9, "High\nArousal")]:
            ax.text(-0.06, y, lbl, ha='center', va='center', fontsize=8,
                    color="#444c56", transform=ax.transAxes, rotation=90)
        ax.axvline(0.5, color="#21262d", lw=1.5, ls='--')
        ax.axhline(0.5, color="#21262d", lw=1.5, ls='--')

        if ax_idx == 0:
            # All edges, coloured by weight
            cmap_e = cm.plasma
            for u, v, d in G.edges(data=True):
                w  = d['weight']
                c  = cmap_e(w)
                alpha = 0.15 + 0.6 * w
                x_vals = [VA_POS[u][0], VA_POS[v][0]]
                y_vals = [VA_POS[u][1], VA_POS[v][1]]
                ax.annotate("", xy=(x_vals[1], y_vals[1]), xytext=(x_vals[0], y_vals[0]),
                            arrowprops=dict(arrowstyle="-|>", color=c,
                                           lw=0.5 + w*2.5, alpha=alpha,
                                           mutation_scale=12))
        else:
            # Only edges on shortest path
            try:
                path = nx.shortest_path(G, source_emo, target_emo, weight=lambda u,v,d: 1-d['weight'])
            except nx.NetworkXNoPath:
                path = [source_emo, target_emo]

            # Draw all edges faint
            for u, v, d in G.edges(data=True):
                x_vals = [VA_POS[u][0], VA_POS[v][0]]
                y_vals = [VA_POS[u][1], VA_POS[v][1]]
                ax.plot(x_vals, y_vals, color="#21262d", lw=0.4, alpha=0.4, zorder=1)

            # Draw path edges bright
            path_edges = list(zip(path, path[1:]))
            for u, v in path_edges:
                x_vals = [VA_POS[u][0], VA_POS[v][0]]
                y_vals = [VA_POS[u][1], VA_POS[v][1]]
                ax.annotate("", xy=(x_vals[1], y_vals[1]), xytext=(x_vals[0], y_vals[0]),
                            arrowprops=dict(arrowstyle="-|>", color="#f78166",
                                           lw=3.0, alpha=0.95, mutation_scale=18), zorder=5)

        # Draw nodes
        for emo in EMO_LIST:
            vx, vy = VA_POS[emo]
            size  = 320 if emo in (source_emo, target_emo) else 180
            ec    = "#ffffff" if emo in (source_emo, target_emo) else "#30363d"
            lw    = 2.5 if emo in (source_emo, target_emo) else 1.0
            ax.scatter(vx, vy, s=size, c=COLORS[emo], edgecolors=ec, linewidths=lw, zorder=6)
            offset_y = 0.06 if vy < 0.8 else -0.07
            ax.annotate(emo.upper(), (vx, vy + offset_y),
                        ha='center', va='center', fontsize=9.5,
                        color=COLORS[emo], fontweight='bold',
                        path_effects=[pe.withStroke(linewidth=3, foreground='#0d1117')])

        ax.set_xlim(-0.05, 1.05); ax.set_ylim(-0.1, 1.1)
        ax.set_xlabel("Valence (negative → positive)", fontsize=9)
        ax.set_ylabel("Arousal (calm → excited)", fontsize=9)

    # Colourbar for edge weights (left panel)
    sm = cm.ScalarMappable(cmap=cm.plasma, norm=Normalize(0, 1))
    sm.set_array([])
    cb = fig.colorbar(sm, ax=axes[0], shrink=0.7, pad=0.02)
    cb.set_label("Edge Weight (VA Similarity)", color="#8b949e", fontsize=8)
    cb.ax.yaxis.set_tick_params(color="#8b949e")
    plt.setp(cb.ax.yaxis.get_ticklabels(), color="#8b949e")

    plt.tight_layout(rect=[0, 0, 1, 0.96])
    _show(fig)
    print("Graph 1: Emotion VA graph rendered.")


