import torch
import torch.nn as nn
import torch.nn.functional as F
from torch_geometric.nn import GCNConv


# ────────────────────────────────────────────────
#          GNN to model frame-level relationships
# ────────────────────────────────────────────────
class EmotionGNN(nn.Module):
    def __init__(self, in_channels=82, hidden=128, out_channels=64):
        super().__init__()
        self.conv1 = GCNConv(in_channels, hidden)
        self.conv2 = GCNConv(hidden, out_channels)
        self.emotion_head = nn.Linear(out_channels, 4)

    def forward(self, x, edge_index):
        x = F.relu(self.conv1(x, edge_index))
        x = self.conv2(x, edge_index)
        pooled = x.mean(dim=0)
        emotion_logits = self.emotion_head(pooled)
        return x, emotion_logits


# ────────────────────────────────────────────────
#         Simple Emotional TTS
# ────────────────────────────────────────────────
class SimpleEmotionalTTS(nn.Module):
    def __init__(self, mel_dim=80, emotion_dim=32, nhead=4, num_layers=2):
        super().__init__()

        self.gnn = EmotionGNN(in_channels=mel_dim + 2, out_channels=64)

        self.variance_adaptor = nn.Sequential(
            nn.Linear(64, mel_dim),
            nn.ReLU(),
        )

        self.emotion_proj = nn.Linear(emotion_dim, mel_dim)
        self.emotion_emb = nn.Embedding(4, emotion_dim)

        self.decoder = nn.TransformerEncoder(
            nn.TransformerEncoderLayer(
                d_model=mel_dim,
                nhead=nhead,
                dim_feedforward=mel_dim * 4,
                dropout=0.1,
                batch_first=True
            ),
            num_layers=num_layers
        )

        self.mel_head = nn.Linear(mel_dim, mel_dim)

    def forward(self, phoneme_features, edge_index, emotion_label):

        gnn_out, emotion_logits = self.gnn(phoneme_features, edge_index)

        adapted = self.variance_adaptor(gnn_out)

        emo_vec = self.emotion_emb(emotion_label)
        emo_vec = self.emotion_proj(emo_vec)

        adapted = adapted + emo_vec  # broadcast

        adapted = adapted.unsqueeze(0)

        decoded = self.decoder(adapted)
        decoded = decoded.squeeze(0)

        mel_pred = self.mel_head(decoded)

        return mel_pred, emotion_logits


# ────────────────────────────────────────────────
#                    Training Demo
# ────────────────────────────────────────────────
def main():
    torch.manual_seed(42)

    model = SimpleEmotionalTTS(mel_dim=80)
    model.train()

    optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)

    # ─── Dummy input ─────────────────────────────────────
    num_frames = 73

    features = torch.randn(num_frames, 82)

    # ✅ FIXED EDGE INDEX
    src = torch.arange(0, num_frames - 1)
    dst = torch.arange(1, num_frames)
    edge_index = torch.stack([src, dst], dim=0)

    emotion_label = torch.tensor([0])

    target_mel = torch.randn(num_frames, 80)

    print("Starting dummy training loop...\n")

    for epoch in range(10):
        optimizer.zero_grad()

        mel_pred, emo_logits = model(features, edge_index, emotion_label)

        loss_mel = F.mse_loss(mel_pred, target_mel)
        loss_emo = F.cross_entropy(emo_logits.unsqueeze(0), emotion_label)

        loss_total = loss_mel + 0.5 * loss_emo

        loss_total.backward()
        optimizer.step()

        print(f"Epoch {epoch:2d} | "
              f"mel = {loss_mel:.4f}   "
              f"emo = {loss_emo:.4f}   "
              f"total = {loss_total:.4f}")

    print("\nTraining finished. No shape errors → model runs correctly.")


if __name__ == "__main__":
    main()

