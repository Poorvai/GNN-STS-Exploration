def main(input_audio_path, target_emotion="angry", output_path="output_emotion.wav"):
    if not os.path.exists(input_audio_path):
        print("❌ Input audio file not found!")
        return

    print("🚀 Processing...")

    # Input Analysis
    analysis = analyze_prosody(input_audio_path)
    print("\n📊 INPUT AUDIO ANALYSIS:")
    print(f"Pitch     : {analysis['pitch_mean']:.1f} Hz")
    print(f"Energy    : {analysis['energy_mean']:.4f}")
    print(f"Silence   : {analysis['silence_ratio']:.1%}")
    print(f"Duration  : {analysis['duration_sec']:.2f} sec")

    # Transcribe text
    text = asr.transcribe(input_audio_path)["text"]
    print(f"\n📝 Transcribed Text: {text}")

    # Reference emotion
    ref_emo = emotion_classifier(input_audio_path)[0]['label']
    print(f"Detected emotion in input: {ref_emo}")

    # Prepare prosody for GNN
    y, sr = analysis["raw_audio"], analysis["sr"]
    f0 = pw.harvest(y.astype(np.float64), sr)[0]
    energy = librosa.feature.rms(y=y)[0]
    num_frames = len(f0)
    if num_frames < 2:
        print("❌ Audio too short!")
        return

    duration_norm = np.full(num_frames, analysis["duration_sec"] / num_frames)
    prosody_seq = np.stack([f0, energy, duration_norm], axis=1).astype(np.float32)

    # Graph for GNN
    edge_list = [[i, i+1] for i in range(num_frames-1)] + [[i+1, i] for i in range(num_frames-1)]
    edge_index = torch.tensor(edge_list, dtype=torch.long).t().contiguous().to(device)
    batch = torch.zeros(num_frames, dtype=torch.long).to(device)
    prosody_tensor = torch.from_numpy(prosody_seq).to(device)

    with torch.no_grad():
        emotion_emb, _ = gnn(prosody_tensor, edge_index, batch)

