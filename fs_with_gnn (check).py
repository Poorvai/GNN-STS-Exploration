# ====================== GNN for Emotion Disentanglement ======================
class EmotionDisentangleGNN(nn.Module):
    def __init__(self, input_dim=3, hidden_dim=64, emotion_dim=32):
        super().__init__()
        self.conv1 = GCNConv(input_dim, hidden_dim)
        self.conv2 = GCNConv(hidden_dim, hidden_dim)
        self.conv3 = GCNConv(hidden_dim, emotion_dim)
        self.emotion_head = nn.Linear(emotion_dim, 5)

    def forward(self, x, edge_index, batch):
        x = F.relu(self.conv1(x, edge_index))
        x = F.relu(self.conv2(x, edge_index))
        x = self.conv3(x, edge_index)
        emotion_emb = global_mean_pool(x, batch)
        emotion_pred = self.emotion_head(emotion_emb)
        return emotion_emb, emotion_pred

